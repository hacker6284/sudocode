//! Zig backend: typed IR → readable Zig 0.16 sources + `sudo_rt.zig` runtime.
//!
//! One `.zig` file per sudo module (`dep.fn` → `dep.fn(...)` via `@import`),
//! plus a shared `sudo_rt.zig`. Traps are a closed Zig error set; functions
//! return `!T`, calls `try`, and `expect_trap` observes an error locally.
//! Value semantics: composite reads are deep-copied at owning store points.
//! Map/Set use a `StringHashMapUnmanaged` keyed by a canonical structural
//! encoding of the key. The entry module carries the TAP runner `main`.
//! See notes/friction-zig.md for the Zig 0.16 stdlib API reference.

use std::collections::{BTreeMap, BTreeSet, HashSet};

use sudoc_ir::{
    BinaryOp, Builtin, IrExpr, IrExprKind, IrFunc, IrMatchArm, IrModule, IrPattern, IrStmt, Place,
    Ty, UnaryOp,
};
use sudoc_sdk::{Backend, GeneratedFile, TestRecipe};

mod reserved;

/// Shared Zig runtime, written alongside every generated module.
pub const RUNTIME: &str = include_str!("runtime/sudo_rt.zig");
pub const RUNTIME_FILE: &str = "sudo_rt.zig";

/// Emit a Zig module for the IR. When `with_tests` is set, `test` blocks
/// become `test_*` functions (the TAP `main` arrives in stage 3).
pub fn emit(module: &IrModule, with_tests: bool, is_entry: bool) -> String {
    let module = reserved::rename_reserved(module);
    emit_with(
        &module,
        std::slice::from_ref(&module),
        with_tests,
        is_entry,
        &BTreeSet::new(),
    )
}

/// Emit `module`, resolving cross-module callee signatures (inout-taking
/// functions in particular) against `all_modules` — the whole program,
/// not just `module` itself. `emit_program` calls this with the real
/// program; `emit` (single-module callers, back-compat) passes a
/// one-element slice and an empty `hoisted` set (fully self-contained file).
fn emit_with(
    module: &IrModule,
    all_modules: &[IrModule],
    with_tests: bool,
    is_entry: bool,
    hoisted: &BTreeSet<String>,
) -> String {
    Emitter {
        m: module,
        all: all_modules,
        out: String::new(),
        indent: 0,
        tmp: 0,
        inouts: HashSet::new(),
        is_entry,
        trap_label: None,
        hoisted,
    }
    .run(with_tests)
}

struct Emitter<'a> {
    m: &'a IrModule,
    /// Every module in the program, for cross-module callee lookup.
    all: &'a [IrModule],
    out: String,
    indent: usize,
    tmp: usize,
    inouts: HashSet<String>,
    is_entry: bool,
    /// Set while emitting an `expect_trap` body: fallible ops break to this
    /// labeled block instead of `try`/`return`-ing out of the test function.
    trap_label: Option<String>,
    /// Mangled type names declared in the shared `sudo_types.zig` file.
    /// Empty for single-module / `emit()` back-compat (everything local).
    hoisted: &'a BTreeSet<String>,
}

impl Emitter<'_> {
    fn run(mut self, with_tests: bool) -> String {
        self.line(&format!(
            "// Generated by sudoc from {}.sudo — do not edit; edit the sudo source.",
            self.m.name
        ));
        // Only `rt` is imported; NaN/Inf literals go through `rt.nan()`/`rt.inf()`
        // so Zig does not flag an unused `std` import on pure-scalar modules.
        self.line("const rt = @import(\"sudo_rt.zig\");");
        for dep in &self.m.imports {
            self.line(&format!("const {dep} = @import(\"{dep}.zig\");"));
        }
        // Collect types before imports finish so we know whether this module
        // needs the shared monomorph file (`st`).
        let types = collect_types(self.m);
        if types.types.keys().any(|k| self.hoisted.contains(k)) {
            self.line("const st = @import(\"sudo_types.zig\");");
        }
        self.blank();

        // Stage-2 monomorphized types + copy/eq/canon helpers.
        self.emit_type_section(&types);

        // Scalars fold at emit time; composites are runtime-filled globals
        // (allocator-backed List/Map/Set cannot be comptime-initialized).
        for c in &self.m.consts {
            let ty = zig_ty(&c.ty);
            if matches!(c.ty, Ty::Int | Ty::Float | Ty::Bool) {
                let value = fold_const(&c.value, self.m);
                self.line(&format!("pub const {}: {ty} = {value};", c.name));
            } else {
                self.line(&format!("pub var {}: {ty} = undefined;", c.name));
            }
        }
        if !self.m.consts.is_empty() {
            self.blank();
        }
        // Always emit: every module can be called uniformly from main() and
        // from each import's transitive init (even modules with no composites
        // of their own may import a dep that has some).
        self.emit_const_init();

        for f in &self.m.funcs {
            self.emit_func(f);
            self.blank();
        }

        if with_tests {
            let names = sudoc_ir::names::test_fn_names(&self.m.tests);
            for (t, name) in self.m.tests.iter().zip(&names) {
                self.inouts.clear();
                self.line(&format!("pub fn {name}() rt.SudoError!void {{"));
                self.indent += 1;
                // Arena is reset after every test; rebuild composite constants
                // (and transitively those of imports) before the body runs.
                self.line("sudoResetConstsReady();");
                self.line("sudoInitConsts();");
                self.emit_stmts(&t.body);
                self.indent -= 1;
                self.line("}");
                self.blank();
            }
            // The entry module carries the TAP runner `main`.
            if self.is_entry {
                self.line("pub fn main() void {");
                self.indent += 1;
                self.line("sudoInitConsts();");
                self.line("const _sudo_tests = [_]rt.TestCase{");
                self.indent += 1;
                for name in &names {
                    self.line(&format!(".{{ .name = \"{name}\", .func = &{name} }},"));
                }
                self.indent -= 1;
                self.line("};");
                self.line("rt.run_tests(&_sudo_tests);");
                self.indent -= 1;
                self.line("}");
            }
        }

        self.out
    }

    /// Idempotent module-constant initializer: transitively init imports, then
    /// build this module's composite constants via the same `store` path used
    /// by function bodies (deep-copies bare `Const` refs).
    ///
    /// The runtime arena is reset between tests (`rt.run_tests`), so composite
    /// constants must be rebuilt each test: tests call `sudoResetConstsReady`
    /// then `sudoInitConsts` (see `run`'s test emission). The ready flag still
    /// makes a single init wave idempotent under diamond-shaped imports.
    fn emit_const_init(&mut self) {
        self.line("var sudo_consts_ready: bool = false;");
        self.line("pub fn sudoResetConstsReady() void {");
        self.indent += 1;
        self.line("sudo_consts_ready = false;");
        for dep in &self.m.imports {
            self.line(&format!("{dep}.sudoResetConstsReady();"));
        }
        self.indent -= 1;
        self.line("}");
        self.blank();
        self.line("pub fn sudoInitConsts() void {");
        self.indent += 1;
        self.line("if (sudo_consts_ready) return;");
        self.line("sudo_consts_ready = true;");
        self.tmp = 0;
        self.inouts.clear();
        for dep in &self.m.imports {
            self.line(&format!("{dep}.sudoInitConsts();"));
        }
        for c in &self.m.consts {
            if matches!(c.ty, Ty::Int | Ty::Float | Ty::Bool) {
                continue;
            }
            let code = self.store(&c.value);
            self.line(&format!("{} = {code};", c.name));
        }
        self.indent -= 1;
        self.line("}");
        self.blank();
    }

    fn emit_type_section(&mut self, types: &TypeSet) {
        if types.types.is_empty() && self.m.records.is_empty() && self.m.enums.is_empty() {
            return;
        }

        // Named record/enum type decls first (enums may self-reference via pointers).
        for r in &self.m.records {
            self.line(&format!("pub const {} = struct {{", r.name));
            self.indent += 1;
            for (fname, fty) in &r.fields {
                self.line(&format!("{fname}: {},", zig_ty(fty)));
            }
            self.indent -= 1;
            self.line("};");
            self.blank();
        }
        for e in &self.m.enums {
            self.line(&format!("pub const {} = union(enum) {{", e.name));
            self.indent += 1;
            for v in &e.variants {
                if v.fields.is_empty() {
                    self.line(&format!("{}: void,", v.name));
                } else if v.fields.len() == 1 {
                    let (fname, fty) = &v.fields[0];
                    let pty = payload_ty(fty);
                    self.line(&format!("{}: {}, // field `{fname}`", v.name, pty));
                } else {
                    self.line(&format!("{}: struct {{", v.name));
                    self.indent += 1;
                    for (fname, fty) in &v.fields {
                        self.line(&format!("{fname}: {},", payload_ty(fty)));
                    }
                    self.indent -= 1;
                    self.line("},");
                }
            }
            self.indent -= 1;
            self.line("};");
            self.blank();
        }

        // Non-hoisted monomorphs (and all records/enums) stay fully local.
        let local = TypeSet {
            types: types
                .types
                .iter()
                .filter(|(k, _)| !self.hoisted.contains(*k))
                .map(|(k, v)| (k.clone(), v.clone()))
                .collect(),
        };

        // Monomorphized aliases (List/Tuple/Option/Result) in topo order.
        let order = topo_order(&local, self.m);
        for name in &order {
            let ty = &local.types[name];
            match ty {
                Ty::List(elem) => {
                    self.line(&format!(
                        "pub const {name} = rt.SudoList({});",
                        zig_ty(elem)
                    ));
                }
                Ty::Tuple(ts) => {
                    self.line(&format!("pub const {name} = struct {{"));
                    self.indent += 1;
                    for (i, t) in ts.iter().enumerate() {
                        self.line(&format!("f{i}: {},", zig_ty(t)));
                    }
                    self.indent -= 1;
                    self.line("};");
                }
                Ty::Option_(inner) => {
                    let inner_z = if boxed_in_payload(inner) {
                        format!("*const {}", zig_ty(inner))
                    } else {
                        zig_ty(inner)
                    };
                    self.line(&format!("pub const {name} = ?{inner_z};"));
                }
                Ty::Result_(ok, err) => {
                    let ok_z = if boxed_in_payload(ok) {
                        format!("*const {}", zig_ty(ok))
                    } else {
                        zig_ty(ok)
                    };
                    let err_z = if boxed_in_payload(err) {
                        format!("*const {}", zig_ty(err))
                    } else {
                        zig_ty(err)
                    };
                    self.line(&format!(
                        "pub const {name} = union(enum) {{ Ok: {ok_z}, Err: {err_z} }};"
                    ));
                }
                Ty::Map(k, v) => {
                    self.line(&format!(
                        "pub const {name} = rt.SudoMap({}, {}, {});",
                        zig_ty(k),
                        zig_ty(v),
                        keyapp_fn(k)
                    ));
                }
                Ty::Set(e) => {
                    self.line(&format!(
                        "pub const {name} = rt.SudoSet({}, {});",
                        zig_ty(e),
                        keyapp_fn(e)
                    ));
                }
                // Record/Enum already emitted above by declaration order.
                Ty::Record(_) | Ty::Enum(_) => {}
                _ => {}
            }
        }
        if !order.is_empty() {
            self.blank();
        }

        // Portable monomorphs live in `sudo_types.zig`; alias so bare mangled
        // names resolve to the single shared nominal type.
        let mut emitted_hoist = false;
        for name in types.types.keys() {
            if !self.hoisted.contains(name) {
                continue;
            }
            self.line(&format!("pub const {name} = st.{name};"));
            self.line(&format!("pub const copy_{name} = st.copy_{name};"));
            self.line(&format!("pub const eq_{name} = st.eq_{name};"));
            self.line(&format!("pub const canon_{name} = st.canon_{name};"));
            emitted_hoist = true;
        }
        if emitted_hoist {
            self.blank();
        }

        // Structural-key encoders for every composite type used as a Map/Set key.
        // Use the full type set so a local non-portable Map/Set can still pull
        // in a portable (hoisted) key type.
        let key_types = collect_key_types(types, self.m);
        let mut emitted_keyapp = false;
        for name in &key_types {
            if self.hoisted.contains(name) {
                self.line(&format!("pub const keyapp_{name} = st.keyapp_{name};"));
                emitted_keyapp = true;
            } else if let Some(ty) = types.types.get(name) {
                let ty = ty.clone();
                self.emit_keyapp(name, &ty);
                emitted_keyapp = true;
            }
        }
        if emitted_keyapp {
            self.blank();
        }

        // copy / eq / canon for every composite monomorph declared locally.
        for name in &order {
            let ty = &local.types[name];
            if !matches!(
                ty,
                Ty::List(_)
                    | Ty::Tuple(_)
                    | Ty::Option_(_)
                    | Ty::Result_(..)
                    | Ty::Record(_)
                    | Ty::Enum(_)
                    | Ty::Map(..)
                    | Ty::Set(_)
            ) {
                continue;
            }
            self.emit_type_ops(name, ty);
        }
    }

    fn emit_type_ops(&mut self, name: &str, ty: &Ty) {
        // ── copy ──
        self.line(&format!("pub fn copy_{name}(v: {name}) {name} {{"));
        self.indent += 1;
        match ty {
            Ty::List(elem) => {
                self.line(&format!("var out: {name} = .{{}};"));
                self.line("_ = &out;");
                self.line("for (v.items()) |x| {");
                self.indent += 1;
                let elem_copy = if needs_dup(elem) {
                    format!("copy_{}(x)", mangle(elem))
                } else {
                    "x".into()
                };
                self.line(&format!("out.append({elem_copy}) catch @panic(\"sudo: arena OOM\");"));
                self.indent -= 1;
                self.line("}");
                self.line("return out;");
            }
            Ty::Tuple(ts) => {
                let mut fields = Vec::new();
                for (i, t) in ts.iter().enumerate() {
                    let src = format!("v.f{i}");
                    fields.push(if needs_dup(t) {
                        format!(".f{i} = copy_{}({src})", mangle(t))
                    } else {
                        format!(".f{i} = {src}")
                    });
                }
                self.line(&format!("return .{{ {} }};", fields.join(", ")));
            }
            Ty::Record(rname) => {
                let r = self.m.record(rname).expect("record");
                let mut fields = Vec::new();
                for (fname, fty) in &r.fields {
                    let src = format!("v.{fname}");
                    fields.push(if needs_dup(fty) {
                        format!(".{fname} = copy_{}({src})", mangle(fty))
                    } else {
                        format!(".{fname} = {src}")
                    });
                }
                self.line(&format!("return .{{ {} }};", fields.join(", ")));
            }
            Ty::Enum(ename) => {
                let e = self.m.enum_(ename).expect("enum");
                self.line("return switch (v) {");
                self.indent += 1;
                for v in &e.variants {
                    if v.fields.is_empty() {
                        self.line(&format!(".{vn} => .{{ .{vn} = {{}} }},", vn = v.name));
                    } else if v.fields.len() == 1 {
                        let (fname, fty) = &v.fields[0];
                        let _ = fname;
                        let payload = if boxed_in_payload(fty) {
                            let inner = if needs_dup(fty) {
                                format!("copy_{}(p.*)", mangle(fty))
                            } else {
                                "p.*".into()
                            };
                            format!("rt.box({}, {inner})", zig_ty(fty))
                        } else if needs_dup(fty) {
                            format!("copy_{}(p)", mangle(fty))
                        } else {
                            "p".into()
                        };
                        self.line(&format!(
                            ".{vn} => |p| .{{ .{vn} = {payload} }},",
                            vn = v.name
                        ));
                    } else {
                        self.line(&format!(".{vn} => |p| .{{ .{vn} = .{{", vn = v.name));
                        self.indent += 1;
                        for (fname, fty) in &v.fields {
                            let field = if boxed_in_payload(fty) {
                                let inner = if needs_dup(fty) {
                                    format!("copy_{}(p.{fname}.*)", mangle(fty))
                                } else {
                                    format!("p.{fname}.*")
                                };
                                format!(
                                    ".{fname} = rt.box({}, {inner}),",
                                    zig_ty(fty)
                                )
                            } else if needs_dup(fty) {
                                format!(".{fname} = copy_{}(p.{fname}),", mangle(fty))
                            } else {
                                format!(".{fname} = p.{fname},")
                            };
                            self.line(&field);
                        }
                        self.indent -= 1;
                        self.line("} },");
                    }
                }
                self.indent -= 1;
                self.line("};");
            }
            Ty::Option_(inner) => {
                if boxed_in_payload(inner) {
                    let copy_inner = if needs_dup(inner) {
                        format!("copy_{}(p.*)", mangle(inner))
                    } else {
                        "p.*".into()
                    };
                    self.line(&format!(
                        "return if (v) |p| rt.box({}, {copy_inner}) else null;",
                        zig_ty(inner)
                    ));
                } else if needs_dup(inner) {
                    self.line(&format!(
                        "return if (v) |x| copy_{}(x) else null;",
                        mangle(inner)
                    ));
                } else {
                    self.line("return v;");
                }
            }
            Ty::Result_(ok, err) => {
                self.line("return switch (v) {");
                self.indent += 1;
                // Ok
                if boxed_in_payload(ok) {
                    let c = if needs_dup(ok) {
                        format!("copy_{}(p.*)", mangle(ok))
                    } else {
                        "p.*".into()
                    };
                    self.line(&format!(
                        ".Ok => |p| .{{ .Ok = rt.box({}, {c}) }},",
                        zig_ty(ok)
                    ));
                } else if needs_dup(ok) {
                    self.line(&format!(".Ok => |x| .{{ .Ok = copy_{}(x) }},", mangle(ok)));
                } else {
                    self.line(".Ok => |x| .{ .Ok = x },");
                }
                // Err
                if boxed_in_payload(err) {
                    let c = if needs_dup(err) {
                        format!("copy_{}(p.*)", mangle(err))
                    } else {
                        "p.*".into()
                    };
                    self.line(&format!(
                        ".Err => |p| .{{ .Err = rt.box({}, {c}) }},",
                        zig_ty(err)
                    ));
                } else if needs_dup(err) {
                    self.line(&format!(".Err => |x| .{{ .Err = copy_{}(x) }},", mangle(err)));
                } else {
                    self.line(".Err => |x| .{ .Err = x },");
                }
                self.indent -= 1;
                self.line("};");
            }
            Ty::Map(k, v) => {
                self.line("var vv = v;");
                self.line("_ = &vv;");
                self.line(&format!("var out: {name} = .{{}};"));
                self.line("_ = &out;");
                self.line("var it = vv.map.valueIterator();");
                self.line("while (it.next()) |kv| {");
                self.indent += 1;
                self.line(&format!(
                    "out.put({}, {});",
                    copy_expr(k, "kv.k"),
                    copy_expr(v, "kv.v")
                ));
                self.indent -= 1;
                self.line("}");
                self.line("return out;");
            }
            Ty::Set(elem) => {
                self.line("var vv = v;");
                self.line("_ = &vv;");
                self.line(&format!("var out: {name} = .{{}};"));
                self.line("_ = &out;");
                self.line("var it = vv.map.valueIterator();");
                self.line("while (it.next()) |ep| {");
                self.indent += 1;
                self.line(&format!("_ = out.add({});", copy_expr(elem, "ep.*")));
                self.indent -= 1;
                self.line("}");
                self.line("return out;");
            }
            _ => self.line("return v;"),
        }
        self.indent -= 1;
        self.line("}");
        self.blank();

        // ── eq ──
        self.line(&format!("pub fn eq_{name}(a: {name}, b: {name}) bool {{"));
        self.indent += 1;
        match ty {
            Ty::List(elem) => {
                self.line("if (a.len() != b.len()) return false;");
                self.line("for (a.items(), b.items()) |x, y| {");
                self.indent += 1;
                let cmp = eq_expr(elem, "x", "y");
                self.line(&format!("if (!({cmp})) return false;"));
                self.indent -= 1;
                self.line("}");
                self.line("return true;");
            }
            Ty::Tuple(ts) => {
                for (i, t) in ts.iter().enumerate() {
                    let cmp = eq_expr(t, &format!("a.f{i}"), &format!("b.f{i}"));
                    self.line(&format!("if (!({cmp})) return false;"));
                }
                self.line("return true;");
            }
            Ty::Record(rname) => {
                let r = self.m.record(rname).expect("record");
                for (fname, fty) in &r.fields {
                    let cmp = eq_expr(fty, &format!("a.{fname}"), &format!("b.{fname}"));
                    self.line(&format!("if (!({cmp})) return false;"));
                }
                self.line("return true;");
            }
            Ty::Enum(ename) => {
                let e = self.m.enum_(ename).expect("enum");
                self.line("return switch (a) {");
                self.indent += 1;
                for v in &e.variants {
                    if v.fields.is_empty() {
                        self.line(&format!(
                            ".{vn} => switch (b) {{ .{vn} => true, else => false }},",
                            vn = v.name
                        ));
                    } else if v.fields.len() == 1 {
                        let (_, fty) = &v.fields[0];
                        self.line(&format!(".{vn} => |pa| switch (b) {{", vn = v.name));
                        self.indent += 1;
                        if boxed_in_payload(fty) {
                            let cmp = eq_expr(fty, "pa.*", "pb.*");
                            self.line(&format!(".{vn} => |pb| {cmp},", vn = v.name));
                        } else {
                            let cmp = eq_expr(fty, "pa", "pb");
                            self.line(&format!(".{vn} => |pb| {cmp},", vn = v.name));
                        }
                        self.line("else => false,");
                        self.indent -= 1;
                        self.line("},");
                    } else {
                        self.line(&format!(".{vn} => |pa| switch (b) {{", vn = v.name));
                        self.indent += 1;
                        self.line(&format!(".{vn} => |pb| {{", vn = v.name));
                        self.indent += 1;
                        for (fname, fty) in &v.fields {
                            let (la, lb) = if boxed_in_payload(fty) {
                                (format!("pa.{fname}.*"), format!("pb.{fname}.*"))
                            } else {
                                (format!("pa.{fname}"), format!("pb.{fname}"))
                            };
                            let cmp = eq_expr(fty, &la, &lb);
                            self.line(&format!("if (!({cmp})) return false;"));
                        }
                        self.line("return true;");
                        self.indent -= 1;
                        self.line("},");
                        self.line("else => false,");
                        self.indent -= 1;
                        self.line("},");
                    }
                }
                self.indent -= 1;
                self.line("};");
            }
            Ty::Option_(inner) => {
                self.line("if (a) |xa| {");
                self.indent += 1;
                self.line("if (b) |xb| {");
                self.indent += 1;
                if boxed_in_payload(inner) {
                    let cmp = eq_expr(inner, "xa.*", "xb.*");
                    self.line(&format!("return {cmp};"));
                } else {
                    let cmp = eq_expr(inner, "xa", "xb");
                    self.line(&format!("return {cmp};"));
                }
                self.indent -= 1;
                self.line("} else return false;");
                self.indent -= 1;
                self.line("} else return b == null;");
            }
            Ty::Result_(ok, err) => {
                self.line("return switch (a) {");
                self.indent += 1;
                self.line(".Ok => |xa| switch (b) {");
                self.indent += 1;
                if boxed_in_payload(ok) {
                    let cmp = eq_expr(ok, "xa.*", "xb.*");
                    self.line(&format!(".Ok => |xb| {cmp},"));
                } else {
                    let cmp = eq_expr(ok, "xa", "xb");
                    self.line(&format!(".Ok => |xb| {cmp},"));
                }
                self.line("else => false,");
                self.indent -= 1;
                self.line("},");
                self.line(".Err => |xa| switch (b) {");
                self.indent += 1;
                if boxed_in_payload(err) {
                    let cmp = eq_expr(err, "xa.*", "xb.*");
                    self.line(&format!(".Err => |xb| {cmp},"));
                } else {
                    let cmp = eq_expr(err, "xa", "xb");
                    self.line(&format!(".Err => |xb| {cmp},"));
                }
                self.line("else => false,");
                self.indent -= 1;
                self.line("},");
                self.indent -= 1;
                self.line("};");
            }
            Ty::Map(_, v) => {
                self.line("if (a.size() != b.size()) return false;");
                self.line("var aa = a;");
                self.line("_ = &aa;");
                self.line("var it = aa.map.valueIterator();");
                self.line("while (it.next()) |kv| {");
                self.indent += 1;
                self.line("if (b.getPtr(kv.k)) |bp| {");
                self.indent += 1;
                let cmp = eq_expr(v, "kv.v", "bp.*");
                self.line(&format!("if (!({cmp})) return false;"));
                self.indent -= 1;
                self.line("} else return false;");
                self.indent -= 1;
                self.line("}");
                self.line("return true;");
            }
            Ty::Set(_) => {
                self.line("if (a.size() != b.size()) return false;");
                self.line("var aa = a;");
                self.line("_ = &aa;");
                self.line("var it = aa.map.valueIterator();");
                self.line("while (it.next()) |ep| {");
                self.indent += 1;
                self.line("if (!b.has(ep.*)) return false;");
                self.indent -= 1;
                self.line("}");
                self.line("return true;");
            }
            _ => self.line("return false;"),
        }
        self.indent -= 1;
        self.line("}");
        self.blank();

        // ── canon ──
        self.line(&format!("pub fn canon_{name}(v: {name}) void {{"));
        self.indent += 1;
        match ty {
            Ty::List(elem) => {
                self.line("rt.det_str(\"[\");");
                self.line("for (v.items(), 0..) |x, i| {");
                self.indent += 1;
                self.line("if (i > 0) rt.det_str(\", \");");
                self.emit_canon_value(elem, "x");
                self.indent -= 1;
                self.line("}");
                self.line("rt.det_str(\"]\");");
            }
            Ty::Tuple(ts) => {
                self.line("rt.det_str(\"[\");");
                for (i, t) in ts.iter().enumerate() {
                    if i > 0 {
                        self.line("rt.det_str(\", \");");
                    }
                    self.emit_canon_value(t, &format!("v.f{i}"));
                }
                self.line("rt.det_str(\"]\");");
            }
            Ty::Record(rname) => {
                let r = self.m.record(rname).expect("record");
                self.line(&format!("rt.det_str(\"{{\\\"r\\\": \\\"{rname}\\\", \\\"v\\\": [\");"));
                for (i, (fname, fty)) in r.fields.iter().enumerate() {
                    if i > 0 {
                        self.line("rt.det_str(\", \");");
                    }
                    self.emit_canon_value(fty, &format!("v.{fname}"));
                }
                self.line("rt.det_str(\"]}\");");
            }
            Ty::Enum(ename) => {
                let e = self.m.enum_(ename).expect("enum");
                self.line("switch (v) {");
                self.indent += 1;
                for v in &e.variants {
                    if v.fields.is_empty() {
                        self.line(&format!(
                            ".{vn} => rt.det_str(\"{{\\\"e\\\": \\\"{ename}.{vn}\\\"}}\"),",
                            vn = v.name
                        ));
                    } else if v.fields.len() == 1 {
                        let (_, fty) = &v.fields[0];
                        self.line(&format!(".{vn} => |p| {{", vn = v.name));
                        self.indent += 1;
                        self.line(&format!(
                            "rt.det_str(\"{{\\\"e\\\": \\\"{ename}.{vn}\\\", \\\"v\\\": [\");",
                            vn = v.name
                        ));
                        if boxed_in_payload(fty) {
                            self.emit_canon_value(fty, "p.*");
                        } else {
                            self.emit_canon_value(fty, "p");
                        }
                        self.line("rt.det_str(\"]}\");");
                        self.indent -= 1;
                        self.line("},");
                    } else {
                        self.line(&format!(".{vn} => |p| {{", vn = v.name));
                        self.indent += 1;
                        self.line(&format!(
                            "rt.det_str(\"{{\\\"e\\\": \\\"{ename}.{vn}\\\", \\\"v\\\": [\");",
                            vn = v.name
                        ));
                        for (i, (fname, fty)) in v.fields.iter().enumerate() {
                            if i > 0 {
                                self.line("rt.det_str(\", \");");
                            }
                            if boxed_in_payload(fty) {
                                self.emit_canon_value(fty, &format!("p.{fname}.*"));
                            } else {
                                self.emit_canon_value(fty, &format!("p.{fname}"));
                            }
                        }
                        self.line("rt.det_str(\"]}\");");
                        self.indent -= 1;
                        self.line("},");
                    }
                }
                self.indent -= 1;
                self.line("}");
            }
            Ty::Option_(inner) => {
                self.line("if (v) |x| {");
                self.indent += 1;
                self.line("rt.det_str(\"{\\\"e\\\": \\\"Option.Some\\\", \\\"v\\\": [\");");
                if boxed_in_payload(inner) {
                    self.emit_canon_value(inner, "x.*");
                } else {
                    self.emit_canon_value(inner, "x");
                }
                self.line("rt.det_str(\"]}\");");
                self.indent -= 1;
                self.line("} else {");
                self.indent += 1;
                self.line("rt.det_str(\"{\\\"e\\\": \\\"Option.None\\\"}\");");
                self.indent -= 1;
                self.line("}");
            }
            Ty::Result_(ok, err) => {
                self.line("switch (v) {");
                self.indent += 1;
                self.line(".Ok => |x| {");
                self.indent += 1;
                self.line("rt.det_str(\"{\\\"e\\\": \\\"Result.Ok\\\", \\\"v\\\": [\");");
                if boxed_in_payload(ok) {
                    self.emit_canon_value(ok, "x.*");
                } else {
                    self.emit_canon_value(ok, "x");
                }
                self.line("rt.det_str(\"]}\");");
                self.indent -= 1;
                self.line("},");
                self.line(".Err => |x| {");
                self.indent += 1;
                self.line("rt.det_str(\"{\\\"e\\\": \\\"Result.Err\\\", \\\"v\\\": [\");");
                if boxed_in_payload(err) {
                    self.emit_canon_value(err, "x.*");
                } else {
                    self.emit_canon_value(err, "x");
                }
                self.line("rt.det_str(\"]}\");");
                self.indent -= 1;
                self.line("},");
                self.indent -= 1;
                self.line("}");
            }
            Ty::Map(k, v) => {
                self.line("var vv = v;");
                self.line("_ = &vv;");
                self.line("rt.det_str(\"{\\\"map\\\": [\");");
                self.line("var it = vv.map.valueIterator();");
                self.line("var i: usize = 0;");
                self.line("while (it.next()) |kv| : (i += 1) {");
                self.indent += 1;
                self.line("if (i > 0) rt.det_str(\", \");");
                self.line("rt.det_str(\"[\");");
                self.emit_canon_value(k, "kv.k");
                self.line("rt.det_str(\", \");");
                self.emit_canon_value(v, "kv.v");
                self.line("rt.det_str(\"]\");");
                self.indent -= 1;
                self.line("}");
                self.line("rt.det_str(\"]}\");");
            }
            Ty::Set(elem) => {
                self.line("var vv = v;");
                self.line("_ = &vv;");
                self.line("rt.det_str(\"{\\\"set\\\": [\");");
                self.line("var it = vv.map.valueIterator();");
                self.line("var i: usize = 0;");
                self.line("while (it.next()) |ep| : (i += 1) {");
                self.indent += 1;
                self.line("if (i > 0) rt.det_str(\", \");");
                self.emit_canon_value(elem, "ep.*");
                self.indent -= 1;
                self.line("}");
                self.line("rt.det_str(\"]}\");");
            }
            _ => {}
        }
        self.indent -= 1;
        self.line("}");
        self.blank();
    }

    /// Append the structural encoding of a composite key type to the scratch
    /// key buffer (`keyapp_<mangle>`). Scalars route to `rt.key_i64/key_bool`.
    fn emit_keyapp(&mut self, name: &str, ty: &Ty) {
        self.line(&format!("pub fn keyapp_{name}(v: {name}) void {{"));
        self.indent += 1;
        match ty {
            Ty::List(elem) => {
                self.line("rt.key_bytes(\"L[\");");
                self.line("for (v.items()) |x| {");
                self.indent += 1;
                self.emit_keyapp_value(elem, "x");
                self.indent -= 1;
                self.line("}");
                self.line("rt.key_bytes(\"]\");");
            }
            Ty::Tuple(ts) => {
                self.line("rt.key_bytes(\"(\");");
                for (i, t) in ts.iter().enumerate() {
                    self.emit_keyapp_value(t, &format!("v.f{i}"));
                }
                self.line("rt.key_bytes(\")\");");
            }
            Ty::Record(rname) => {
                let r = self.m.record(rname).expect("record");
                self.line(&format!("rt.key_bytes(\"R{{{rname}:\");"));
                for (fname, fty) in &r.fields {
                    self.emit_keyapp_value(fty, &format!("v.{fname}"));
                }
                self.line("rt.key_bytes(\"}\");");
            }
            Ty::Enum(ename) => {
                let e = self.m.enum_(ename).expect("enum");
                self.line("switch (v) {");
                self.indent += 1;
                for var in &e.variants {
                    if var.fields.is_empty() {
                        self.line(&format!(
                            ".{vn} => rt.key_bytes(\"E{vn};\"),",
                            vn = var.name
                        ));
                    } else if var.fields.len() == 1 {
                        let (_, fty) = &var.fields[0];
                        self.line(&format!(".{vn} => |p| {{", vn = var.name));
                        self.indent += 1;
                        self.line(&format!("rt.key_bytes(\"E{vn}(\");", vn = var.name));
                        let acc = if boxed_in_payload(fty) { "p.*" } else { "p" };
                        self.emit_keyapp_value(fty, acc);
                        self.line("rt.key_bytes(\")\");");
                        self.indent -= 1;
                        self.line("},");
                    } else {
                        self.line(&format!(".{vn} => |p| {{", vn = var.name));
                        self.indent += 1;
                        self.line(&format!("rt.key_bytes(\"E{vn}(\");", vn = var.name));
                        for (fname, fty) in &var.fields {
                            let acc = if boxed_in_payload(fty) {
                                format!("p.{fname}.*")
                            } else {
                                format!("p.{fname}")
                            };
                            self.emit_keyapp_value(fty, &acc);
                        }
                        self.line("rt.key_bytes(\")\");");
                        self.indent -= 1;
                        self.line("},");
                    }
                }
                self.indent -= 1;
                self.line("}");
            }
            Ty::Option_(inner) => {
                self.line("if (v) |p| {");
                self.indent += 1;
                self.line("rt.key_bytes(\"S(\");");
                let acc = if boxed_in_payload(inner) { "p.*" } else { "p" };
                self.emit_keyapp_value(inner, acc);
                self.line("rt.key_bytes(\")\");");
                self.indent -= 1;
                self.line("} else rt.key_bytes(\"N;\");");
            }
            Ty::Result_(ok, err) => {
                self.line("switch (v) {");
                self.indent += 1;
                let ok_acc = if boxed_in_payload(ok) { "p.*" } else { "p" };
                self.line(".Ok => |p| {");
                self.indent += 1;
                self.line("rt.key_bytes(\"K(\");");
                self.emit_keyapp_value(ok, ok_acc);
                self.line("rt.key_bytes(\")\");");
                self.indent -= 1;
                self.line("},");
                let err_acc = if boxed_in_payload(err) { "p.*" } else { "p" };
                self.line(".Err => |p| {");
                self.indent += 1;
                self.line("rt.key_bytes(\"X(\");");
                self.emit_keyapp_value(err, err_acc);
                self.line("rt.key_bytes(\")\");");
                self.indent -= 1;
                self.line("},");
                self.indent -= 1;
                self.line("}");
            }
            _ => {}
        }
        self.indent -= 1;
        self.line("}");
        self.blank();
    }

    fn emit_keyapp_value(&mut self, ty: &Ty, expr: &str) {
        match ty {
            Ty::Int => self.line(&format!("rt.key_i64({expr});")),
            Ty::Bool => self.line(&format!("rt.key_bool({expr});")),
            Ty::Float => unsupported("float in map/set key"),
            _ => self.line(&format!("keyapp_{}({expr});", mangle(ty))),
        }
    }

    fn emit_canon_value(&mut self, ty: &Ty, expr: &str) {
        match ty {
            Ty::Int => self.line(&format!("rt.det_i64({expr});")),
            Ty::Float => self.line(&format!("rt.det_f64({expr});")),
            Ty::Bool => self.line(&format!("rt.det_bool({expr});")),
            Ty::Func { .. } => self.line("rt.det_str(\"<func>\");"),
            _ => self.line(&format!("canon_{}({expr});", mangle(ty))),
        }
    }

    fn emit_func(&mut self, f: &IrFunc) {
        self.inouts = f
            .params
            .iter()
            .filter(|p| p.inout)
            .map(|p| p.name.clone())
            .collect();

        // Params that the body writes to (rebind / field / index / inout arg /
        // mutating method) need a mutable local, since Zig params are const.
        let written = written_params(f, self.m, self.all);

        let mut params = Vec::new();
        let mut shadows = Vec::new();
        for p in &f.params {
            let ty = zig_ty(&p.ty);
            if p.inout {
                params.push(format!("{}: *{ty}", p.name));
            } else if written.contains(&p.name) {
                params.push(format!("{}_arg: {ty}", p.name));
                shadows.push(p.name.clone());
            } else {
                params.push(format!("{}: {ty}", p.name));
            }
        }
        let ret = match &f.ret {
            Some(t) => format!("rt.SudoError!{}", zig_ty(t)),
            None => "rt.SudoError!void".into(),
        };
        self.line(&format!(
            "pub fn {}({}) {ret} {{",
            f.name,
            params.join(", ")
        ));
        self.indent += 1;
        // Rebind written params to owned, mutable locals (call-site `store`
        // already deep-copied managed args, so this shallow rebind is enough).
        for n in &shadows {
            self.line(&format!("var {n} = {n}_arg;"));
            self.line(&format!("_ = &{n};"));
        }
        self.emit_stmts(&f.body);
        self.indent -= 1;
        self.line("}");
    }

    fn emit_stmts(&mut self, stmts: &[IrStmt]) {
        for s in stmts {
            self.emit_stmt(s);
        }
    }

    fn emit_stmt(&mut self, s: &IrStmt) {
        match s {
            IrStmt::Assign {
                target,
                value,
                declares,
            } => {
                // Index assign → list.put
                if let Place::Index {
                    base,
                    base_ty,
                    index,
                } = target
                {
                    match base_ty {
                        Ty::List(_) => {
                            let base_lv = self.place_lvalue(base);
                            let idx = self.expr(index);
                            let val = self.store(value);
                            self.try_line(&format!("{base_lv}.put({idx}, {val})"));
                        }
                        Ty::Map(..) => {
                            // Insert-or-overwrite; key and value are owned copies.
                            let base_lv = self.place_lvalue(base);
                            let key = self.store(index);
                            let val = self.store(value);
                            self.line(&format!("{base_lv}.put({key}, {val});"));
                        }
                        _ => unsupported("assign to non-list/map index"),
                    }
                    return;
                }

                let val = self.store(value);
                let lhs = self.place_lvalue(target);
                if *declares {
                    let ty = zig_ty(&value.ty);
                    self.line(&format!("var {lhs}: {ty} = {val};"));
                    // Zig 0.16: unused mutation of `var` is a hard error.
                    self.line(&format!("_ = &{lhs};"));
                } else {
                    self.line(&format!("{lhs} = {val};"));
                }
            }
            IrStmt::TupleAssign {
                targets,
                declares,
                value,
            } => {
                let tup = self.store(value);
                let tmp = self.tmp("tup");
                let ty = zig_ty(&value.ty);
                self.line(&format!("const {tmp}: {ty} = {tup};"));
                if let Ty::Tuple(ts) = &value.ty {
                    for (i, (name, decl)) in targets.iter().zip(declares.iter()).enumerate() {
                        let elem_ty = &ts[i];
                        let src = format!("{tmp}.f{i}");
                        let owned = if needs_dup(elem_ty) {
                            format!("copy_{}({src})", mangle(elem_ty))
                        } else {
                            src
                        };
                        if *decl {
                            self.line(&format!("var {name}: {} = {owned};", zig_ty(elem_ty)));
                            self.line(&format!("_ = &{name};"));
                        } else {
                            self.line(&format!("{name} = {owned};"));
                        }
                    }
                } else {
                    unsupported("TupleAssign non-tuple");
                }
            }
            IrStmt::Expr(e) => {
                let v = self.expr(e);
                self.line(&format!("_ = {v};"));
            }
            IrStmt::If { arms, else_block } => {
                self.emit_if_chain(arms, else_block.as_deref(), 0);
            }
            IrStmt::While { cond, body } => {
                if can_trap(cond) {
                    self.line("while (true) {");
                    self.indent += 1;
                    let c = self.expr(cond);
                    self.line(&format!("if (!({c})) break;"));
                    self.emit_stmts(body);
                    self.indent -= 1;
                    self.line("}");
                } else {
                    let c = self.expr(cond);
                    self.line(&format!("while ({c}) {{"));
                    self.indent += 1;
                    self.emit_stmts(body);
                    self.indent -= 1;
                    self.line("}");
                }
            }
            IrStmt::ForRange {
                var,
                from,
                to,
                down,
                body,
            } => {
                let lo = self.expr(from);
                let hi = self.expr(to);
                let lo_t = self.tmp("lo");
                let hi_t = self.tmp("hi");
                let done = self.tmp("done");
                self.line(&format!("const {lo_t}: i64 = {lo};"));
                self.line(&format!("const {hi_t}: i64 = {hi};"));
                self.line(&format!("var {var}: i64 = {lo_t};"));
                self.line(&format!("_ = &{var};"));
                if *down {
                    self.line(&format!("var {done} = !({lo_t} >= {hi_t});"));
                    self.line(&format!("_ = &{done};"));
                    self.line(&format!("while (!{done}) : ({var} -%= 1) {{"));
                } else {
                    self.line(&format!("var {done} = !({lo_t} <= {hi_t});"));
                    self.line(&format!("_ = &{done};"));
                    self.line(&format!("while (!{done}) : ({var} +%= 1) {{"));
                }
                self.indent += 1;
                self.line(&format!("{done} = ({var} == {hi_t});"));
                self.emit_stmts(body);
                self.indent -= 1;
                self.line("}");
            }
            IrStmt::ForIn { vars, iter, body } => {
                self.emit_for_in(vars, iter, body);
            }
            IrStmt::Match { scrutinee, arms } => {
                self.emit_match(scrutinee, arms);
            }
            IrStmt::Return(None) => self.line("return;"),
            IrStmt::Return(Some(e)) => {
                let v = self.store(e);
                self.line(&format!("return {v};"));
            }
            IrStmt::Assert { cond, line } => self.emit_assert(cond, *line),
            IrStmt::Skip => {}
            IrStmt::Break => self.line("break;"),
            IrStmt::Continue => self.line("continue;"),
            IrStmt::ExpectTrap { kind, body, line } => self.emit_expect_trap(kind, body, *line),
        }
    }

    /// Emit `if` / nested `else { if ... }` chains so short-circuit temps from
    /// `else if` conditions stay in a scope that is still open when they are
    /// read (flat `else if` would declare them inside the preceding arm).
    fn emit_if_chain(
        &mut self,
        arms: &[(IrExpr, Vec<IrStmt>)],
        else_block: Option<&[IrStmt]>,
        idx: usize,
    ) {
        let (cond, body) = &arms[idx];
        let c = self.expr(cond);
        self.line(&format!("if ({c}) {{"));
        self.indent += 1;
        self.emit_stmts(body);
        self.indent -= 1;
        if idx + 1 < arms.len() {
            self.line("} else {");
            self.indent += 1;
            self.emit_if_chain(arms, else_block, idx + 1);
            self.indent -= 1;
            self.line("}");
        } else if let Some(eb) = else_block {
            self.line("} else {");
            self.indent += 1;
            self.emit_stmts(eb);
            self.indent -= 1;
            self.line("}");
        } else {
            self.line("}");
        }
    }

    /// `expect_trap Kind` observes a trap locally. The body is lowered inline
    /// into a labeled block that yields `?rt.SudoError` — fallible ops break to
    /// the label carrying the error instead of `try`/`return`-ing out of the
    /// test. A labeled block is an ordinary scope, so the body sees enclosing
    /// locals exactly as normal code does (Zig nested fns, by contrast, do NOT
    /// close over outer locals — see notes/friction-zig.md).
    fn emit_expect_trap(&mut self, kind: &str, body: &[IrStmt], line: u32) {
        let trap = self.tmp("trap");
        let label = self.tmp("et");
        let e = self.tmp("e");
        self.line(&format!("const {trap}: ?rt.SudoError = {label}: {{"));
        self.indent += 1;
        let saved = self.trap_label.replace(label.clone());
        self.emit_stmts(body);
        self.trap_label = saved;
        self.line(&format!("break :{label} null;"));
        self.indent -= 1;
        self.line("};");
        self.line(&format!("if ({trap}) |{e}| {{"));
        self.indent += 1;
        self.line(&format!(
            "if ({e} != rt.SudoError.{kind}) return rt.expectTrapWrong({line}, \"{kind}\", @errorName({e}));"
        ));
        self.indent -= 1;
        self.line(&format!("}} else return rt.expectTrapNone({line}, \"{kind}\");"));
    }

    fn emit_for_in(&mut self, vars: &[String], iter: &IrExpr, body: &[IrStmt]) {
        // Snapshot the collection so mutating it in the body cannot affect the
        // iteration. Loop bindings are fresh owned copies (value semantics);
        // emit them as `var … ; _ = &name;` so unused / never-mutated bindings
        // are not Zig 0.16 compile errors.
        let coll = self.expr(iter);
        let snap = self.tmp("snap");
        let snap_ty = zig_ty(&iter.ty);
        let snap_val = if needs_dup(&iter.ty) {
            format!("copy_{}({coll})", mangle(&iter.ty))
        } else {
            coll
        };
        self.line(&format!("var {snap}: {snap_ty} = {snap_val};"));
        self.line(&format!("_ = &{snap};"));
        match &iter.ty {
            Ty::List(elem) => {
                let vname = &vars[0];
                self.line(&format!("for ({snap}.items()) |{vname}_ref| {{"));
                self.indent += 1;
                self.bind_loop_var(vname, elem, &format!("{vname}_ref"));
                self.emit_stmts(body);
                self.indent -= 1;
                self.line("}");
            }
            Ty::Set(elem) => {
                let it = self.tmp("it");
                self.line(&format!("var {it} = {snap}.map.valueIterator();"));
                self.line(&format!("while ({it}.next()) |ep| {{"));
                self.indent += 1;
                self.bind_loop_var(&vars[0], elem, "ep.*");
                self.emit_stmts(body);
                self.indent -= 1;
                self.line("}");
            }
            Ty::Map(k, v) => {
                let it = self.tmp("it");
                self.line(&format!("var {it} = {snap}.map.valueIterator();"));
                self.line(&format!("while ({it}.next()) |kv| {{"));
                self.indent += 1;
                self.bind_loop_var(&vars[0], k, "kv.k");
                if vars.len() >= 2 {
                    self.bind_loop_var(&vars[1], v, "kv.v");
                }
                self.emit_stmts(body);
                self.indent -= 1;
                self.line("}");
            }
            other => unsupported(&format!("stmt ForIn on {other}")),
        }
    }

    /// Bind a for-in loop variable to an owned copy of the current element.
    fn bind_loop_var(&mut self, name: &str, elem_ty: &Ty, src: &str) {
        let owned = copy_expr(elem_ty, src);
        self.line(&format!("var {name} = {owned};"));
        self.line(&format!("_ = &{name};"));
    }

    fn emit_match(&mut self, scrutinee: &IrExpr, arms: &[IrMatchArm]) {
        let s = self.expr(scrutinee);
        let s_tmp = self.tmp("scrut");
        self.line(&format!("const {s_tmp} = {s};"));

        match &scrutinee.ty {
            Ty::Int | Ty::Bool => {
                self.line(&format!("switch ({s_tmp}) {{"));
                self.indent += 1;
                let mut has_else = false;
                let mut saw_true = false;
                let mut saw_false = false;
                let mut seen_ints: std::collections::HashSet<i64> =
                    std::collections::HashSet::new();
                for arm in arms {
                    match &arm.pattern {
                        IrPattern::Int(v) => {
                            // First-matching-case-wins (spec §6.3): a later
                            // arm whose literal already appeared is dead
                            // code. A Zig `switch` can't express that with a
                            // duplicate case value, so drop it.
                            if !seen_ints.insert(*v) {
                                continue;
                            }
                            self.line(&format!("{v} => {{"));
                            self.indent += 1;
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        IrPattern::Bool(v) => {
                            // Same first-wins dedup, for the bool literal
                            // that's already been seen.
                            if *v {
                                if saw_true {
                                    continue;
                                }
                                saw_true = true;
                            } else {
                                if saw_false {
                                    continue;
                                }
                                saw_false = true;
                            }
                            self.line(&format!("{v} => {{"));
                            self.indent += 1;
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        IrPattern::Wildcard => {
                            has_else = true;
                            self.line("else => {");
                            self.indent += 1;
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        IrPattern::Variant { .. } => {
                            unsupported("int/bool match with variant pattern")
                        }
                    }
                }
                // Zig switches must be exhaustive: i64 always needs an else;
                // bool needs one unless both values are covered explicitly.
                let bool_exhaustive =
                    matches!(scrutinee.ty, Ty::Bool) && saw_true && saw_false;
                if !has_else && !bool_exhaustive {
                    self.line("else => {},");
                }
                self.indent -= 1;
                self.line("}");
            }
            Ty::Enum(ename) => {
                let edef = self.m.enum_(ename).expect("enum");
                self.line(&format!("switch ({s_tmp}) {{"));
                self.indent += 1;
                let mut has_else = false;
                for arm in arms {
                    match &arm.pattern {
                        IrPattern::Wildcard => {
                            has_else = true;
                            self.line("else => {");
                            self.indent += 1;
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        IrPattern::Variant {
                            variant, binders, ..
                        } => {
                            let vdef = edef
                                .variants
                                .iter()
                                .find(|v| v.name == *variant)
                                .expect("variant");
                            if vdef.fields.is_empty() {
                                self.line(&format!(".{variant} => {{"));
                                self.indent += 1;
                                self.emit_stmts(&arm.body);
                                self.indent -= 1;
                                self.line("},");
                            } else if vdef.fields.len() == 1 {
                                let (_, fty) = &vdef.fields[0];
                                let b = &binders[0];
                                self.line(&format!(".{variant} => |{b}_p| {{"));
                                self.indent += 1;
                                if boxed_in_payload(fty) {
                                    let owned = if needs_dup(fty) {
                                        format!("copy_{}({b}_p.*)", mangle(fty))
                                    } else {
                                        format!("{b}_p.*")
                                    };
                                    self.line(&format!("const {b} = {owned};"));
                                } else if needs_dup(fty) {
                                    self.line(&format!(
                                        "const {b} = copy_{}({b}_p);",
                                        mangle(fty)
                                    ));
                                } else {
                                    self.line(&format!("const {b} = {b}_p;"));
                                }
                                if !binder_used(&arm.body, b) {
                                    self.line(&format!("_ = {b};"));
                                }
                                self.emit_stmts(&arm.body);
                                self.indent -= 1;
                                self.line("},");
                            } else {
                                self.line(&format!(".{variant} => |{variant}_p| {{"));
                                self.indent += 1;
                                for (i, (fname, fty)) in vdef.fields.iter().enumerate() {
                                    let b = &binders[i];
                                    if boxed_in_payload(fty) {
                                        let owned = if needs_dup(fty) {
                                            format!(
                                                "copy_{}({variant}_p.{fname}.*)",
                                                mangle(fty)
                                            )
                                        } else {
                                            format!("{variant}_p.{fname}.*")
                                        };
                                        self.line(&format!("const {b} = {owned};"));
                                    } else if needs_dup(fty) {
                                        self.line(&format!(
                                            "const {b} = copy_{}({variant}_p.{fname});",
                                            mangle(fty)
                                        ));
                                    } else {
                                        self.line(&format!(
                                            "const {b} = {variant}_p.{fname};"
                                        ));
                                    }
                                    if !binder_used(&arm.body, b) {
                                        self.line(&format!("_ = {b};"));
                                    }
                                }
                                self.emit_stmts(&arm.body);
                                self.indent -= 1;
                                self.line("},");
                            }
                        }
                        IrPattern::Int(_) | IrPattern::Bool(_) => {
                            unsupported("enum match with scalar pattern")
                        }
                    }
                }
                // Enum matches are exhaustive (checker-guaranteed); a Zig `else`
                // is emitted only when the sudo match used a wildcard.
                let _ = has_else;
                self.indent -= 1;
                self.line("}");
            }
            Ty::Option_(inner) => {
                // Two-way: Some binder / None / wildcard.
                let mut some_arm: Option<&IrMatchArm> = None;
                let mut none_arm: Option<&IrMatchArm> = None;
                let mut wild_arm: Option<&IrMatchArm> = None;
                for arm in arms {
                    match &arm.pattern {
                        IrPattern::Variant { variant, .. } if variant == "Some" => {
                            some_arm = Some(arm);
                        }
                        IrPattern::Variant { variant, .. } if variant == "None" => {
                            none_arm = Some(arm);
                        }
                        IrPattern::Wildcard => wild_arm = Some(arm),
                        _ => {}
                    }
                }
                self.line(&format!("if ({s_tmp}) |__opt_p| {{"));
                self.indent += 1;
                if let Some(arm) = some_arm {
                    if let IrPattern::Variant { binders, .. } = &arm.pattern {
                        let b = &binders[0];
                        if boxed_in_payload(inner) {
                            let owned = if needs_dup(inner) {
                                format!("copy_{}(__opt_p.*)", mangle(inner))
                            } else {
                                "__opt_p.*".into()
                            };
                            self.line(&format!("const {b} = {owned};"));
                        } else if needs_dup(inner) {
                            self.line(&format!(
                                "const {b} = copy_{}(__opt_p);",
                                mangle(inner)
                            ));
                        } else {
                            self.line(&format!("const {b} = __opt_p;"));
                        }
                        if !binder_used(&arm.body, b) {
                            self.line(&format!("_ = {b};"));
                        }
                    }
                    self.emit_stmts(&arm.body);
                } else if let Some(arm) = wild_arm {
                    self.emit_stmts(&arm.body);
                }
                self.indent -= 1;
                self.line("} else {");
                self.indent += 1;
                if let Some(arm) = none_arm {
                    self.emit_stmts(&arm.body);
                } else if let Some(arm) = wild_arm {
                    // wildcard only fires once — if Some already used it, skip
                    if some_arm.is_none() {
                        self.emit_stmts(&arm.body);
                    }
                }
                self.indent -= 1;
                self.line("}");
            }
            Ty::Result_(ok, err) => {
                self.line(&format!("switch ({s_tmp}) {{"));
                self.indent += 1;
                let mut has_else = false;
                for arm in arms {
                    match &arm.pattern {
                        IrPattern::Wildcard => {
                            has_else = true;
                            self.line("else => {");
                            self.indent += 1;
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        IrPattern::Variant {
                            variant, binders, ..
                        } => {
                            let (side_ty, tag) = if variant == "Ok" {
                                (ok.as_ref(), "Ok")
                            } else {
                                (err.as_ref(), "Err")
                            };
                            let b = &binders[0];
                            self.line(&format!(".{tag} => |{b}_p| {{"));
                            self.indent += 1;
                            if boxed_in_payload(side_ty) {
                                let owned = if needs_dup(side_ty) {
                                    format!("copy_{}({b}_p.*)", mangle(side_ty))
                                } else {
                                    format!("{b}_p.*")
                                };
                                self.line(&format!("const {b} = {owned};"));
                            } else if needs_dup(side_ty) {
                                self.line(&format!(
                                    "const {b} = copy_{}({b}_p);",
                                    mangle(side_ty)
                                ));
                            } else {
                                self.line(&format!("const {b} = {b}_p;"));
                            }
                            if !binder_used(&arm.body, b) {
                                self.line(&format!("_ = {b};"));
                            }
                            self.emit_stmts(&arm.body);
                            self.indent -= 1;
                            self.line("},");
                        }
                        _ => unsupported("result match pattern"),
                    }
                }
                // Ok/Err are exhaustive; emit `else` only for a wildcard arm.
                let _ = has_else;
                self.indent -= 1;
                self.line("}");
            }
            other => unsupported(&format!("match on {other}")),
        }
    }

    fn emit_assert(&mut self, cond: &IrExpr, line: u32) {
        if let IrExprKind::Binary {
            op: BinaryOp::Eq,
            lhs,
            rhs,
        } = &cond.kind
        {
            if is_scalar(&lhs.ty) && is_scalar(&rhs.ty) {
                let l = self.expr(lhs);
                let r = self.expr(rhs);
                let helper = match &lhs.ty {
                    Ty::Int => "assertEqI64",
                    Ty::Float => "assertEqF64",
                    Ty::Bool => "assertEqBool",
                    _ => unreachable!(),
                };
                self.try_line(&format!("rt.{helper}({l}, {r}, {line})"));
                return;
            }
            // Composite equality with canon diagnostics.
            if !is_scalar(&lhs.ty)
                && !matches!(lhs.ty, Ty::Func { .. } | Ty::Map(..) | Ty::Set(_))
            {
                let l = self.expr(lhs);
                let r = self.expr(rhs);
                let mn = mangle(&lhs.ty);
                self.line(&format!("if (!eq_{mn}({l}, {r})) {{"));
                self.indent += 1;
                self.line("rt.det_reset();");
                self.line(&format!("rt.det_line_prefix({line});"));
                self.line(&format!("canon_{mn}({l});"));
                self.line("rt.det_str(\" != \");");
                self.line(&format!("canon_{mn}({r});"));
                let raise = self.raise("rt.SudoError.AssertFailed");
                self.line(&format!("{raise};"));
                self.indent -= 1;
                self.line("}");
                return;
            }
        }
        let c = self.expr(cond);
        let raise = self.raise("rt.SudoError.AssertFailed");
        self.line(&format!("if (!({c})) {raise};"));
    }

    /// Storing-position expression: deep-copy when reading an alias of a
    /// managed type.
    fn store(&mut self, e: &IrExpr) -> String {
        let code = self.expr(e);
        if aliasing(&e.kind) && needs_dup(&e.ty) {
            format!("copy_{}({code})", mangle(&e.ty))
        } else {
            code
        }
    }

    /// Expression → Zig source fragment. May emit preceding statements (temps)
    /// at the current indent when statement-lowering is required (lazy and/or).
    fn expr(&mut self, e: &IrExpr) -> String {
        match &e.kind {
            IrExprKind::Int(v) => format!("{v}"),
            IrExprKind::Float(v) => format_float(*v),
            IrExprKind::Bool(v) => {
                if *v {
                    "true".into()
                } else {
                    "false".into()
                }
            }
            IrExprKind::Text(chars) => {
                // Text is List<int> of code points.
                let t = self.tmp("txt");
                let ty = zig_ty(&e.ty);
                self.line(&format!("var {t}: {ty} = .{{}};"));
                self.line(&format!("_ = &{t};"));
                for c in chars {
                    self.line(&format!("{t}.append({c}) catch @panic(\"sudo: arena OOM\");"));
                }
                t
            }
            IrExprKind::Local(n) => {
                if self.inouts.contains(n) {
                    format!("{n}.*")
                } else {
                    n.clone()
                }
            }
            IrExprKind::Const(n) => n.clone(),
            IrExprKind::FuncRef(n) => format!("&{}", qualify_name(n)),
            IrExprKind::List(xs) => {
                let t = self.tmp("lst");
                let ty = zig_ty(&e.ty);
                self.line(&format!("var {t}: {ty} = .{{}};"));
                self.line(&format!("_ = &{t};"));
                for x in xs {
                    let v = self.store(x);
                    self.line(&format!("{t}.append({v}) catch @panic(\"sudo: arena OOM\");"));
                }
                t
            }
            IrExprKind::Tuple(xs) => {
                let mut fields = Vec::new();
                for (i, x) in xs.iter().enumerate() {
                    fields.push(format!(".f{i} = {}", self.store(x)));
                }
                let ty = zig_ty(&e.ty);
                format!("@as({ty}, .{{ {} }})", fields.join(", "))
            }
            IrExprKind::CallFunc { name, args } => self.emit_call(name, args),
            IrExprKind::CallValue { callee, args } => {
                let c = self.expr(callee);
                let a: Vec<String> = args.iter().map(|x| self.store(x)).collect();
                self.tryx(&format!("{c}({})", a.join(", ")))
            }
            IrExprKind::NewRecord { name, args } => {
                let r = self.m.record(name).expect("record");
                let mut fields = Vec::new();
                for ((fname, _), a) in r.fields.iter().zip(args.iter()) {
                    fields.push(format!(".{fname} = {}", self.store(a)));
                }
                format!("@as({name}, .{{ {} }})", fields.join(", "))
            }
            IrExprKind::NewVariant {
                enum_name,
                variant,
                args,
            } => self.emit_new_variant(enum_name, variant, args, &e.ty),
            IrExprKind::Unary { op, operand } => self.emit_unary(*op, operand, &e.ty),
            IrExprKind::Binary { op, lhs, rhs } => self.emit_binary(*op, lhs, rhs, &e.ty),
            IrExprKind::Builtin { builtin, args } => self.emit_builtin(*builtin, args, &e.ty),
            IrExprKind::MutBuiltin {
                builtin,
                recv,
                recv_ty,
                args,
            } => self.emit_mut_builtin(*builtin, recv, recv_ty, args, &e.ty),
            IrExprKind::GetField { recv, name } => {
                let r = self.expr(recv);
                format!("{r}.{name}")
            }
            IrExprKind::Index { recv, index } => {
                let r = self.expr(recv);
                let i = self.expr(index);
                match &recv.ty {
                    Ty::List(_) => self.tryx(&format!("{r}.at({i})")),
                    Ty::Map(..) => self.tryx(&format!("{r}.index({i})")),
                    other => unsupported(&format!("expr Index on {other}")),
                }
            }
        }
    }

    fn emit_new_variant(
        &mut self,
        enum_name: &str,
        variant: &str,
        args: &[IrExpr],
        result_ty: &Ty,
    ) -> String {
        let result_z = zig_ty(result_ty);
        // Option / Result are not user enums in the module table.
        if enum_name == "Option" {
            if variant == "None" {
                return format!("@as({result_z}, null)");
            }
            // Some(x)
            let inner = &args[0];
            let v = self.store(inner);
            if boxed_in_payload(&inner.ty) {
                return format!(
                    "@as({result_z}, rt.box({}, {v}))",
                    zig_ty(&inner.ty)
                );
            }
            return format!("@as({result_z}, {v})");
        }
        if enum_name == "Result" {
            let inner = &args[0];
            let v = self.store(inner);
            let payload = if boxed_in_payload(&inner.ty) {
                format!("rt.box({}, {v})", zig_ty(&inner.ty))
            } else {
                v
            };
            return if variant == "Ok" {
                format!("@as({result_z}, .{{ .Ok = {payload} }})")
            } else {
                format!("@as({result_z}, .{{ .Err = {payload} }})")
            };
        }

        let edef = self.m.enum_(enum_name).expect("enum");
        let vdef = edef
            .variants
            .iter()
            .find(|v| v.name == variant)
            .expect("variant");
        if vdef.fields.is_empty() {
            format!("@as({result_z}, .{{ .{variant} = {{}} }})")
        } else if vdef.fields.len() == 1 {
            let (_, fty) = &vdef.fields[0];
            let v = self.store(&args[0]);
            let payload = if boxed_in_payload(fty) {
                format!("rt.box({}, {v})", zig_ty(fty))
            } else {
                v
            };
            format!("@as({result_z}, .{{ .{variant} = {payload} }})")
        } else {
            let mut fields = Vec::new();
            for ((fname, fty), a) in vdef.fields.iter().zip(args.iter()) {
                let v = self.store(a);
                let payload = if boxed_in_payload(fty) {
                    format!("rt.box({}, {v})", zig_ty(fty))
                } else {
                    v
                };
                fields.push(format!(".{fname} = {payload}"));
            }
            format!(
                "@as({result_z}, .{{ .{variant} = .{{ {} }} }})",
                fields.join(", ")
            )
        }
    }

    fn emit_unary(&mut self, op: UnaryOp, operand: &IrExpr, ty: &Ty) -> String {
        let x = self.expr(operand);
        match op {
            UnaryOp::Neg => match ty {
                Ty::Int => self.tryx(&format!("rt.neg({x})")),
                Ty::Float => format!("(-{x})"),
                _ => unreachable!("neg on non-numeric"),
            },
            UnaryOp::Not => format!("(!{x})"),
        }
    }

    fn emit_binary(&mut self, op: BinaryOp, lhs: &IrExpr, rhs: &IrExpr, ty: &Ty) -> String {
        if matches!(op, BinaryOp::And | BinaryOp::Or) {
            if can_trap(rhs) {
                return self.emit_lazy_bool(op, lhs, rhs);
            }
            let l = self.expr(lhs);
            let r = self.expr(rhs);
            let kw = if op == BinaryOp::And { "and" } else { "or" };
            return format!("({l} {kw} {r})");
        }

        // Composite == / !=
        if matches!(op, BinaryOp::Eq | BinaryOp::Ne) && !is_scalar(&lhs.ty) {
            let l = self.expr(lhs);
            let r = self.expr(rhs);
            if matches!(lhs.ty, Ty::Func { .. }) {
                return if op == BinaryOp::Eq {
                    format!("({l} == {r})")
                } else {
                    format!("({l} != {r})")
                };
            }
            let eq = format!("eq_{}({l}, {r})", mangle(&lhs.ty));
            return if op == BinaryOp::Eq {
                format!("({eq})")
            } else {
                format!("(!{eq})")
            };
        }

        // List (and text) concatenation.
        if matches!(op, BinaryOp::Add) && matches!(ty, Ty::List(_)) {
            return self.emit_list_concat(lhs, rhs, ty);
        }

        let l = self.expr(lhs);
        let r = self.expr(rhs);

        if matches!(ty, Ty::Int) {
            match op {
                BinaryOp::Add => return self.tryx(&format!("rt.add({l}, {r})")),
                BinaryOp::Sub => return self.tryx(&format!("rt.sub({l}, {r})")),
                BinaryOp::Mul => return self.tryx(&format!("rt.mul({l}, {r})")),
                BinaryOp::Div => return self.tryx(&format!("rt.divFloor({l}, {r})")),
                BinaryOp::Mod => return self.tryx(&format!("rt.modFloor({l}, {r})")),
                _ => {}
            }
        }

        if matches!(ty, Ty::Float) {
            match op {
                BinaryOp::Add => return format!("({l} + {r})"),
                BinaryOp::Sub => return format!("({l} - {r})"),
                BinaryOp::Mul => return format!("({l} * {r})"),
                BinaryOp::Div => return format!("rt.fdiv({l}, {r})"),
                _ => {}
            }
        }

        let sym = match op {
            BinaryOp::Lt => "<",
            BinaryOp::Le => "<=",
            BinaryOp::Gt => ">",
            BinaryOp::Ge => ">=",
            BinaryOp::Eq => "==",
            BinaryOp::Ne => "!=",
            BinaryOp::Add
            | BinaryOp::Sub
            | BinaryOp::Mul
            | BinaryOp::Div
            | BinaryOp::Mod
            | BinaryOp::And
            | BinaryOp::Or => unreachable!("handled above"),
        };
        format!("({l} {sym} {r})")
    }

    /// `a + b` on lists: a fresh list of deep-copied elements from each side.
    fn emit_list_concat(&mut self, lhs: &IrExpr, rhs: &IrExpr, ty: &Ty) -> String {
        let elem = match ty {
            Ty::List(e) => e.as_ref().clone(),
            _ => unreachable!("list concat result is a list"),
        };
        let l = self.expr(lhs);
        let lt = self.tmp("lcat");
        self.line(&format!("var {lt}: {} = {l};", zig_ty(&lhs.ty)));
        self.line(&format!("_ = &{lt};"));
        let r = self.expr(rhs);
        let rt = self.tmp("rcat");
        self.line(&format!("var {rt}: {} = {r};", zig_ty(&rhs.ty)));
        self.line(&format!("_ = &{rt};"));
        let out = self.tmp("cat");
        self.line(&format!("var {out}: {} = .{{}};", zig_ty(ty)));
        self.line(&format!("_ = &{out};"));
        let copied = copy_expr(&elem, "x");
        for src in [&lt, &rt] {
            self.line(&format!("for ({src}.items()) |x| {{"));
            self.indent += 1;
            self.line(&format!("{out}.append({copied}) catch @panic(\"sudo: arena OOM\");"));
            self.indent -= 1;
            self.line("}");
        }
        out
    }

    fn emit_lazy_bool(&mut self, op: BinaryOp, lhs: &IrExpr, rhs: &IrExpr) -> String {
        let t = self.tmp("b");
        let l = self.expr(lhs);
        self.line(&format!("var {t}: bool = {l};"));
        self.line(&format!("_ = &{t};"));
        match op {
            BinaryOp::And => {
                self.line(&format!("if ({t}) {{"));
                self.indent += 1;
                let r = self.expr(rhs);
                self.line(&format!("{t} = {r};"));
                self.indent -= 1;
                self.line("}");
            }
            BinaryOp::Or => {
                self.line(&format!("if (!{t}) {{"));
                self.indent += 1;
                let r = self.expr(rhs);
                self.line(&format!("{t} = {r};"));
                self.indent -= 1;
                self.line("}");
            }
            _ => unreachable!(),
        }
        t
    }

    fn resolve_func(&self, name: &str) -> Option<&IrFunc> {
        resolve_func_in(self.m, self.all, name)
    }

    fn emit_call(&mut self, name: &str, args: &[IrExpr]) -> String {
        let inout_flags: Vec<bool> = self
            .resolve_func(name)
            .map(|f| f.params.iter().map(|p| p.inout).collect())
            .unwrap_or_else(|| vec![false; args.len()]);

        let mut parts = Vec::new();
        for (i, arg) in args.iter().enumerate() {
            let is_inout = inout_flags.get(i).copied().unwrap_or(false);
            if is_inout {
                parts.push(self.expr_as_inout_arg(arg));
            } else {
                parts.push(self.store(arg));
            }
        }
        let callee = qualify_name(name);
        self.tryx(&format!("{callee}({})", parts.join(", ")))
    }

    fn expr_as_inout_arg(&mut self, e: &IrExpr) -> String {
        let place = expr_as_place(e);
        let lv = self.place_lvalue(&place);
        if let Place::Var(n) = &place {
            if self.inouts.contains(n) {
                return n.clone();
            }
        }
        format!("&{lv}")
    }

    fn place_lvalue(&self, p: &Place) -> String {
        match p {
            Place::Var(n) => {
                if self.inouts.contains(n) {
                    format!("{n}.*")
                } else {
                    n.clone()
                }
            }
            Place::Index { .. } => {
                // Index assign handled specially in emit_stmt; lvalue form
                // should not be requested for list elements.
                unsupported("place Index as bare lvalue")
            }
            Place::Field { base, name, .. } => {
                let b = self.place_lvalue(base);
                format!("{b}.{name}")
            }
        }
    }

    /// Pointer to a place (for mutating list methods / put).
    fn place_ptr(&self, p: &Place) -> String {
        match p {
            Place::Var(n) => {
                if self.inouts.contains(n) {
                    // Already a `*T` parameter.
                    n.clone()
                } else {
                    format!("&{n}")
                }
            }
            Place::Field { base, name, .. } => {
                let b = self.place_lvalue(base);
                format!("&({b}.{name})")
            }
            Place::Index { .. } => unsupported("place_ptr Index"),
        }
    }

    fn emit_builtin(&mut self, b: Builtin, args: &[IrExpr], result_ty: &Ty) -> String {
        match b {
            Builtin::AbsInt => {
                let a = self.expr(&args[0]);
                self.tryx(&format!("rt.absInt({a})"))
            }
            Builtin::AbsFloat => {
                let a = self.expr(&args[0]);
                format!("rt.absFloat({a})")
            }
            Builtin::MinInt => {
                let a = self.expr(&args[0]);
                let b = self.expr(&args[1]);
                format!("@min({a}, {b})")
            }
            Builtin::MaxInt => {
                let a = self.expr(&args[0]);
                let b = self.expr(&args[1]);
                format!("@max({a}, {b})")
            }
            Builtin::MinFloat => {
                let a = self.expr(&args[0]);
                let b = self.expr(&args[1]);
                format!("rt.fmin({a}, {b})")
            }
            Builtin::MaxFloat => {
                let a = self.expr(&args[0]);
                let b = self.expr(&args[1]);
                format!("rt.fmax({a}, {b})")
            }
            Builtin::FloatOfInt => {
                let a = self.expr(&args[0]);
                format!("rt.floatOfInt({a})")
            }
            Builtin::IntOfFloat => {
                let a = self.expr(&args[0]);
                self.tryx(&format!("rt.intOfFloat({a})"))
            }
            Builtin::Floor => {
                let a = self.expr(&args[0]);
                format!("rt.floor({a})")
            }
            Builtin::Ceil => {
                let a = self.expr(&args[0]);
                format!("rt.ceil({a})")
            }
            Builtin::Round => {
                let a = self.expr(&args[0]);
                format!("rt.round({a})")
            }
            Builtin::Sqrt => {
                let a = self.expr(&args[0]);
                format!("rt.sqrt({a})")
            }
            Builtin::Filled => {
                // filled(n, value) → List
                let n = self.expr(&args[0]);
                let n_t = self.tmp("n");
                self.line(&format!("const {n_t}: i64 = {n};"));
                let raise = self.raise("rt.SudoError.InvalidArg");
                self.line(&format!("if ({n_t} < 0) {raise};"));
                let t = self.tmp("filled");
                let ty = zig_ty(result_ty);
                self.line(&format!("var {t}: {ty} = .{{}};"));
                self.line(&format!("_ = &{t};"));
                let i = self.tmp("i");
                self.line(&format!("var {i}: i64 = 0;"));
                self.line(&format!("_ = &{i};"));
                self.line(&format!("while ({i} < {n_t}) : ({i} +%= 1) {{"));
                self.indent += 1;
                let v = self.store(&args[1]);
                self.line(&format!("{t}.append({v}) catch @panic(\"sudo: arena OOM\");"));
                self.indent -= 1;
                self.line("}");
                t
            }
            Builtin::ListLength => {
                let a = self.expr(&args[0]);
                format!("{a}.len()")
            }
            Builtin::OptIsSome => {
                let a = self.expr(&args[0]);
                format!("({a} != null)")
            }
            Builtin::OptIsNone => {
                let a = self.expr(&args[0]);
                format!("({a} == null)")
            }
            Builtin::OptUnwrap => {
                let a = self.expr(&args[0]);
                let raise = self.raise("rt.SudoError.UnwrapFailed");
                // Boxed payloads deref after the null check.
                if matches!(&args[0].ty, Ty::Option_(inner) if boxed_in_payload(inner)) {
                    format!("(({a} orelse {raise}).*)")
                } else {
                    format!("({a} orelse {raise})")
                }
            }
            Builtin::OptGetOr => {
                let a = self.expr(&args[0]);
                let d = self.store(&args[1]);
                if let Ty::Option_(inner) = &args[0].ty {
                    if boxed_in_payload(inner) {
                        // `?*const T` vs default `T` — branch explicitly.
                        let t = self.tmp("og");
                        let ty = zig_ty(inner);
                        self.line(&format!("const {t}: {ty} = if ({a}) |p| p.* else {d};"));
                        t
                    } else {
                        format!("({a} orelse {d})")
                    }
                } else {
                    format!("({a} orelse {d})")
                }
            }
            Builtin::ResIsOk => {
                let a = self.expr(&args[0]);
                // Tag check via switch — more portable than `v == .Ok` across Zig
                // versions when the union carries payloads.
                let t = self.tmp("isok");
                self.line(&format!("const {t}: bool = switch ({a}) {{ .Ok => true, .Err => false }};"));
                t
            }
            Builtin::ResIsErr => {
                let a = self.expr(&args[0]);
                let t = self.tmp("iserr");
                self.line(&format!("const {t}: bool = switch ({a}) {{ .Ok => false, .Err => true }};"));
                t
            }
            Builtin::ResUnwrap => {
                let a = self.expr(&args[0]);
                let t = self.tmp("ru");
                // Need the Ok payload type from result_ty… actually unwrap
                // returns the Ok side. Infer from args[0].ty.
                if let Ty::Result_(ok, _) = &args[0].ty {
                    let ok_z = zig_ty(ok);
                    self.line(&format!("const {t}: {ok_z} = switch ({a}) {{"));
                    self.indent += 1;
                    if boxed_in_payload(ok) {
                        self.line(".Ok => |p| p.*,");
                    } else {
                        self.line(".Ok => |x| x,");
                    }
                    let raise = self.raise("rt.SudoError.UnwrapFailed");
                    self.line(&format!(".Err => {raise},"));
                    self.indent -= 1;
                    self.line("};");
                    t
                } else {
                    unsupported("ResUnwrap non-result")
                }
            }
            Builtin::ResGetOr => {
                let a = self.expr(&args[0]);
                let d = self.store(&args[1]);
                let t = self.tmp("rg");
                if let Ty::Result_(ok, _) = &args[0].ty {
                    let ok_z = zig_ty(ok);
                    self.line(&format!("const {t}: {ok_z} = switch ({a}) {{"));
                    self.indent += 1;
                    if boxed_in_payload(ok) {
                        self.line(".Ok => |p| p.*,");
                    } else {
                        self.line(".Ok => |x| x,");
                    }
                    self.line(&format!(".Err => {d},"));
                    self.indent -= 1;
                    self.line("};");
                    t
                } else {
                    unsupported("ResGetOr non-result")
                }
            }
            // Mutating list builtins appear as MutBuiltin when they mutate;
            // ListAppend as expression-method may still show as Builtin in
            // some lowerings — handle both.
            Builtin::ListAppend
            | Builtin::ListPop
            | Builtin::ListInsert
            | Builtin::ListRemoveAt
            | Builtin::ListSwap
            | Builtin::ListSort => unsupported(&format!(
                "builtin {b:?} as non-mutating (expected MutBuiltin)"
            )),
            Builtin::NewMap | Builtin::NewSet => {
                let t = self.tmp("coll");
                let ty = zig_ty(result_ty);
                self.line(&format!("var {t}: {ty} = .{{}};"));
                self.line(&format!("_ = &{t};"));
                t
            }
            Builtin::MapSize | Builtin::SetSize => {
                let a = self.expr(&args[0]);
                format!("{a}.size()")
            }
            Builtin::MapHas => {
                let m = self.expr(&args[0]);
                let k = self.expr(&args[1]);
                format!("{m}.has({k})")
            }
            Builtin::SetHas => {
                let s = self.expr(&args[0]);
                let v = self.expr(&args[1]);
                format!("{s}.has({v})")
            }
            Builtin::MapGet => {
                // Map.get(k) → Option<V>: deep-copy the value; box composite payload.
                let m = self.expr(&args[0]);
                let k = self.expr(&args[1]);
                let inner = match result_ty {
                    Ty::Option_(v) => v.as_ref(),
                    _ => unreachable!("MapGet result is Option"),
                };
                let copied = copy_expr(inner, "p.*");
                let payload = if boxed_in_payload(inner) {
                    format!("rt.box({}, {copied})", zig_ty(inner))
                } else {
                    copied
                };
                let t = self.tmp("get");
                let ty = zig_ty(result_ty);
                self.line(&format!(
                    "const {t}: {ty} = if ({m}.getPtr({k})) |p| {payload} else null;"
                ));
                t
            }
            Builtin::MapKeys => {
                let key_ty = match &args[0].ty {
                    Ty::Map(k, _) => k.as_ref().clone(),
                    _ => unreachable!("MapKeys on non-map"),
                };
                self.emit_collect_list(&args[0], result_ty, "kv.k", &key_ty)
            }
            Builtin::MapValues => {
                let val_ty = match &args[0].ty {
                    Ty::Map(_, v) => v.as_ref().clone(),
                    _ => unreachable!("MapValues on non-map"),
                };
                self.emit_collect_list(&args[0], result_ty, "kv.v", &val_ty)
            }
            Builtin::SetItems => {
                let elem_ty = match &args[0].ty {
                    Ty::Set(e) => e.as_ref().clone(),
                    _ => unreachable!("SetItems on non-set"),
                };
                self.emit_collect_list(&args[0], result_ty, "kv.*", &elem_ty)
            }
            Builtin::MapDelete
            | Builtin::SetAdd
            | Builtin::SetRemove => unsupported(&format!(
                "builtin {b:?} as non-mutating (expected MutBuiltin)"
            )),
        }
    }

    /// Build a `List<E>` by iterating a Map/Set snapshot, copying each element.
    /// `elem_expr` is `kv.k` / `kv.v` (map) or `kv.*` (set value iterator).
    fn emit_collect_list(
        &mut self,
        coll: &IrExpr,
        result_ty: &Ty,
        elem_expr: &str,
        elem_ty: &Ty,
    ) -> String {
        let c = self.expr(coll);
        let snap = self.tmp("snap");
        let snap_ty = zig_ty(&coll.ty);
        self.line(&format!("var {snap}: {snap_ty} = {c};"));
        self.line(&format!("_ = &{snap};"));
        let out = self.tmp("out");
        let out_ty = zig_ty(result_ty);
        self.line(&format!("var {out}: {out_ty} = .{{}};"));
        self.line(&format!("_ = &{out};"));
        let it = self.tmp("it");
        self.line(&format!("var {it} = {snap}.map.valueIterator();"));
        self.line(&format!("while ({it}.next()) |kv| {{"));
        self.indent += 1;
        let copied = copy_expr(elem_ty, elem_expr);
        self.line(&format!("{out}.append({copied}) catch @panic(\"sudo: arena OOM\");"));
        self.indent -= 1;
        self.line("}");
        out
    }

    /// Lvalue for a `MutBuiltin` receiver. Unlike `place_lvalue` (bare
    /// locals / fields-of-locals only), this also handles receivers reached
    /// through a list/map index (`xs[0].append(v)`, `m[k].append(v)`,
    /// `p.field[i].append(v)`) by borrowing the element in place via
    /// `atPtr`/`getPtr` and calling the mutating method through the
    /// resulting pointer (Zig auto-derefs pointer receivers for method
    /// calls, so callers can use this exactly like `place_lvalue`'s result).
    fn mut_recv_lvalue(&mut self, p: &Place) -> String {
        if let Place::Index { base, base_ty, index } = p {
            let base_lv = self.place_lvalue(base);
            let idx = self.expr(index);
            return match base_ty {
                Ty::List(_) => self.tryx(&format!("{base_lv}.atPtr({idx})")),
                Ty::Map(..) => {
                    let raise = self.raise("rt.SudoError.KeyMissing");
                    format!("({base_lv}.getPtr({idx}) orelse {raise})")
                }
                other => unsupported(&format!("mut recv Index on {other:?}")),
            };
        }
        self.place_lvalue(p)
    }

    fn emit_mut_builtin(
        &mut self,
        b: Builtin,
        recv: &Place,
        recv_ty: &Ty,
        args: &[IrExpr],
        result_ty: &Ty,
    ) -> String {
        // Method-style mutators take an lvalue receiver (Zig auto-refs);
        // only the free-function sort helpers need a real `*T` pointer.
        let recv_lv = self.mut_recv_lvalue(recv);
        let _ = result_ty;
        match b {
            Builtin::ListAppend => {
                let v = self.store(&args[0]);
                self.try_line(&format!("{recv_lv}.append({v})"));
                // Void result for expression context (`_ = {}`).
                "{}".into()
            }
            Builtin::ListPop => {
                self.tryx(&format!("{recv_lv}.pop()"))
            }
            Builtin::ListInsert => {
                let idx = self.expr(&args[0]);
                let v = self.store(&args[1]);
                self.try_line(&format!("{recv_lv}.insert({idx}, {v})"));
                "{}".into()
            }
            Builtin::ListRemoveAt => {
                let idx = self.expr(&args[0]);
                self.tryx(&format!("{recv_lv}.removeAt({idx})"))
            }
            Builtin::ListSwap => {
                let i = self.expr(&args[0]);
                let j = self.expr(&args[1]);
                self.try_line(&format!("{recv_lv}.swap({i}, {j})"));
                "{}".into()
            }
            Builtin::ListSort => {
                let ptr = self.place_ptr(recv);
                match recv_ty {
                    Ty::List(elem) if matches!(elem.as_ref(), Ty::Int) => {
                        self.line(&format!("rt.sortI64({ptr});"));
                    }
                    Ty::List(elem) if matches!(elem.as_ref(), Ty::Float) => {
                        self.line(&format!("rt.sortF64({ptr});"));
                    }
                    _ => unsupported("ListSort non int/float"),
                }
                "{}".into()
            }
            Builtin::MapDelete => {
                let k = self.store(&args[0]);
                format!("{recv_lv}.delete({k})")
            }
            Builtin::SetAdd => {
                let v = self.store(&args[0]);
                format!("{recv_lv}.add({v})")
            }
            Builtin::SetRemove => {
                let v = self.expr(&args[0]);
                format!("{recv_lv}.remove({v})")
            }
            other => unsupported(&format!("mut builtin {other:?}")),
        }
    }

    fn tmp(&mut self, tag: &str) -> String {
        let n = self.tmp;
        self.tmp += 1;
        format!("_sudo_{tag}{n}")
    }

    /// A fallible call as an expression. Normally `(try X)`; inside an
    /// `expect_trap` block, `(X catch |e| break :label e)` so the trap is
    /// observed locally rather than propagating out of the test function.
    /// Zig labeled blocks are ordinary scopes (not namespace boundaries), so
    /// the body still sees enclosing locals — no capture, unlike a nested fn.
    fn tryx(&self, inner: &str) -> String {
        match &self.trap_label {
            Some(l) => format!("({inner} catch |_sudo_e| break :{l} _sudo_e)"),
            None => format!("(try {inner})"),
        }
    }

    /// Emit a fallible statement `try X;` (or the `catch break` form).
    fn try_line(&mut self, inner: &str) {
        let s = self.tryx(inner);
        self.line(&format!("{s};"));
    }

    /// Raise a trap: `return ERR` normally; `break :label ERR` in expect_trap.
    fn raise(&self, err: &str) -> String {
        match &self.trap_label {
            Some(l) => format!("break :{l} {err}"),
            None => format!("return {err}"),
        }
    }

    fn line(&mut self, s: &str) {
        for _ in 0..self.indent {
            self.out.push_str("    ");
        }
        self.out.push_str(s);
        self.out.push('\n');
    }

    fn blank(&mut self) {
        self.out.push('\n');
    }
}

// ── helpers ─────────────────────────────────────────────────────────────────

fn unsupported(what: &str) -> ! {
    panic!("stage 2: {what} not yet implemented");
}

fn is_scalar(ty: &Ty) -> bool {
    matches!(ty, Ty::Int | Ty::Float | Ty::Bool)
}

/// Payload field of enum/Option/Result: box Record/Enum/Option/Result/Tuple.
fn boxed_in_payload(ty: &Ty) -> bool {
    matches!(
        ty,
        Ty::Record(_) | Ty::Enum(_) | Ty::Option_(_) | Ty::Result_(..) | Ty::Tuple(_)
    )
}

fn payload_ty(ty: &Ty) -> String {
    if boxed_in_payload(ty) {
        format!("*const {}", zig_ty(ty))
    } else {
        zig_ty(ty)
    }
}

fn mangle(ty: &Ty) -> String {
    sudoc_ir::mangle::mangle_ty(ty)
}

/// True when `ty` contains no module-local `Record`/`Enum` anywhere in its
/// shape — safe to declare once in `sudo_types.zig` and share across modules.
fn is_portable(ty: &Ty) -> bool {
    match ty {
        Ty::Int | Ty::Float | Ty::Bool => true,
        Ty::Record(_) | Ty::Enum(_) => false,
        Ty::List(e) | Ty::Set(e) | Ty::Option_(e) => is_portable(e),
        Ty::Map(k, v) => is_portable(k) && is_portable(v),
        Ty::Result_(a, b) => is_portable(a) && is_portable(b),
        Ty::Tuple(ts) => ts.iter().all(is_portable),
        Ty::Func { params, ret } => {
            params.iter().all(is_portable)
                && ret.as_ref().map(|r| is_portable(r)).unwrap_or(true)
        }
        Ty::Infer(_) => unreachable!("Infer escaped the checker"),
    }
}

fn zig_ty(ty: &Ty) -> String {
    match ty {
        Ty::Int => "i64".into(),
        Ty::Float => "f64".into(),
        Ty::Bool => "bool".into(),
        Ty::List(_)
        | Ty::Tuple(_)
        | Ty::Option_(_)
        | Ty::Result_(..)
        | Ty::Record(_)
        | Ty::Enum(_)
        | Ty::Map(..)
        | Ty::Set(_) => mangle(ty),
        Ty::Func { params, ret } => {
            let ps: Vec<String> = params.iter().map(zig_ty).collect();
            let r = match ret {
                Some(r) => zig_ty(r),
                None => "void".into(),
            };
            format!("*const fn ({}) rt.SudoError!{r}", ps.join(", "))
        }
        Ty::Infer(_) => unreachable!("Infer escaped the checker"),
    }
}

fn needs_dup(ty: &Ty) -> bool {
    match ty {
        Ty::List(_) | Ty::Map(..) | Ty::Set(_) | Ty::Record(_) | Ty::Enum(_) => true,
        Ty::Tuple(ts) => ts.iter().any(needs_dup),
        Ty::Option_(t) => needs_dup(t) || boxed_in_payload(t),
        Ty::Result_(a, b) => {
            needs_dup(a) || needs_dup(b) || boxed_in_payload(a) || boxed_in_payload(b)
        }
        _ => false,
    }
}

fn aliasing(kind: &IrExprKind) -> bool {
    match kind {
        IrExprKind::Local(_)
        | IrExprKind::Const(_)
        | IrExprKind::GetField { .. }
        | IrExprKind::Index { .. } => true,
        IrExprKind::Builtin { builtin, .. } => matches!(
            builtin,
            Builtin::OptUnwrap | Builtin::OptGetOr | Builtin::ResUnwrap | Builtin::ResGetOr
        ),
        _ => false,
    }
}

fn eq_expr(ty: &Ty, a: &str, b: &str) -> String {
    if is_scalar(ty) || matches!(ty, Ty::Func { .. }) {
        format!("({a} == {b})")
    } else {
        format!("eq_{}({a}, {b})", mangle(ty))
    }
}

/// Deep-copy `e` if the type owns heap; otherwise pass through.
fn copy_expr(ty: &Ty, e: &str) -> String {
    if needs_dup(ty) {
        format!("copy_{}({e})", mangle(ty))
    } else {
        e.to_string()
    }
}

/// The `appendKey` function passed to `rt.SudoMap`/`rt.SudoSet` for key type `ty`.
fn keyapp_fn(ty: &Ty) -> String {
    match ty {
        Ty::Int => "rt.key_i64".into(),
        Ty::Bool => "rt.key_bool".into(),
        Ty::Float => unsupported("float map/set key"),
        _ => format!("keyapp_{}", mangle(ty)),
    }
}

/// Composite types that need a `keyapp_*` encoder: every Map/Set key type and
/// its composite sub-parts (scalars route directly to `rt.key_i64/key_bool`).
fn collect_key_types(types: &TypeSet, m: &IrModule) -> Vec<String> {
    fn add_composite(ty: &Ty, m: &IrModule, out: &mut BTreeSet<String>) {
        match ty {
            Ty::Int | Ty::Bool | Ty::Float => {}
            Ty::List(e) | Ty::Option_(e) => {
                out.insert(mangle(ty));
                add_composite(e, m, out);
            }
            Ty::Tuple(ts) => {
                out.insert(mangle(ty));
                for t in ts {
                    add_composite(t, m, out);
                }
            }
            Ty::Result_(a, b) => {
                out.insert(mangle(ty));
                add_composite(a, m, out);
                add_composite(b, m, out);
            }
            Ty::Record(n) if out.insert(mangle(ty)) => {
                if let Some(r) = m.record(n) {
                    for (_, t) in &r.fields {
                        add_composite(t, m, out);
                    }
                }
            }
            Ty::Enum(n) if out.insert(mangle(ty)) => {
                if let Some(e) = m.enum_(n) {
                    for v in &e.variants {
                        for (_, t) in &v.fields {
                            add_composite(t, m, out);
                        }
                    }
                }
            }
            // Map/Set/Func are never key-eligible (the checker guarantees it).
            _ => {}
        }
    }
    let mut out = BTreeSet::new();
    for ty in types.types.values() {
        match ty {
            Ty::Map(k, _) => add_composite(k, m, &mut out),
            Ty::Set(e) => add_composite(e, m, &mut out),
            _ => {}
        }
    }
    out.into_iter().collect()
}

fn qualify_name(name: &str) -> String {
    name.to_string()
}

// ── Written-parameter analysis (const params need a mutable shadow) ─────────

/// Non-inout parameters the body writes to (rebind, field/index assignment,
/// mutating method receiver, or passed as an inout argument).
fn written_params(f: &IrFunc, m: &IrModule, all: &[IrModule]) -> HashSet<String> {
    let params: HashSet<String> = f
        .params
        .iter()
        .filter(|p| !p.inout)
        .map(|p| p.name.clone())
        .collect();
    let mut out = HashSet::new();
    collect_written(&f.body, m, all, &params, &mut out);
    out
}

fn place_root(p: &Place) -> &str {
    match p {
        Place::Var(n) => n,
        Place::Field { base, .. } | Place::Index { base, .. } => place_root(base),
    }
}

fn expr_root_var(e: &IrExpr) -> Option<&str> {
    match &e.kind {
        IrExprKind::Local(n) => Some(n),
        IrExprKind::GetField { recv, .. } => expr_root_var(recv),
        IrExprKind::Index { recv, .. } => expr_root_var(recv),
        _ => None,
    }
}

/// Resolve a callee's signature across the whole program: a bare name
/// looks in `m` (the current module); a `module.func` qualified name
/// looks up the named module in `all`. Mirrors the Haskell backend's
/// `lookupFunc`/`splitQual` (backends/haskell/Emit.hs).
fn resolve_func_in<'a>(m: &'a IrModule, all: &'a [IrModule], name: &str) -> Option<&'a IrFunc> {
    match name.split_once('.') {
        Some((modname, fname)) => all.iter().find(|mm| mm.name == modname)?.func(fname),
        None => m.func(name),
    }
}

/// Whether `name` is referenced anywhere in `stmts` (match-arm body). Used
/// to emit `_ = binder;` for deliberately unused variant binders so Zig
/// does not reject them as unused local constants.
fn binder_used(stmts: &[IrStmt], name: &str) -> bool {
    fn expr_uses(e: &IrExpr, name: &str) -> bool {
        match &e.kind {
            IrExprKind::Local(n) => n == name,
            IrExprKind::List(xs)
            | IrExprKind::Tuple(xs)
            | IrExprKind::CallFunc { args: xs, .. }
            | IrExprKind::NewRecord { args: xs, .. }
            | IrExprKind::NewVariant { args: xs, .. }
            | IrExprKind::Builtin { args: xs, .. } => xs.iter().any(|x| expr_uses(x, name)),
            IrExprKind::CallValue { callee, args } => {
                expr_uses(callee, name) || args.iter().any(|x| expr_uses(x, name))
            }
            IrExprKind::MutBuiltin { recv, args, .. } => {
                place_uses(recv, name) || args.iter().any(|x| expr_uses(x, name))
            }
            IrExprKind::GetField { recv, .. } => expr_uses(recv, name),
            IrExprKind::Index { recv, index } => {
                expr_uses(recv, name) || expr_uses(index, name)
            }
            IrExprKind::Unary { operand, .. } => expr_uses(operand, name),
            IrExprKind::Binary { lhs, rhs, .. } => {
                expr_uses(lhs, name) || expr_uses(rhs, name)
            }
            _ => false,
        }
    }
    fn place_uses(p: &Place, name: &str) -> bool {
        match p {
            Place::Var(n) => n == name,
            Place::Index { base, index, .. } => {
                place_uses(base, name) || expr_uses(index, name)
            }
            Place::Field { base, .. } => place_uses(base, name),
        }
    }
    stmts.iter().any(|s| match s {
        IrStmt::Assign { target, value, .. } => {
            place_uses(target, name) || expr_uses(value, name)
        }
        IrStmt::TupleAssign { targets, value, .. } => {
            targets.iter().any(|t| t == name) || expr_uses(value, name)
        }
        IrStmt::Expr(e) => expr_uses(e, name),
        IrStmt::If { arms, else_block } => {
            arms.iter().any(|(c, b)| expr_uses(c, name) || binder_used(b, name))
                || else_block.as_ref().is_some_and(|b| binder_used(b, name))
        }
        IrStmt::While { cond, body } => expr_uses(cond, name) || binder_used(body, name),
        IrStmt::ForRange { from, to, body, .. } => {
            expr_uses(from, name) || expr_uses(to, name) || binder_used(body, name)
        }
        IrStmt::ForIn { iter, body, .. } => expr_uses(iter, name) || binder_used(body, name),
        IrStmt::Match { scrutinee, arms } => {
            expr_uses(scrutinee, name) || arms.iter().any(|a| binder_used(&a.body, name))
        }
        IrStmt::Return(Some(e)) => expr_uses(e, name),
        IrStmt::Assert { cond, .. } => expr_uses(cond, name),
        IrStmt::ExpectTrap { body, .. } => binder_used(body, name),
        IrStmt::Return(None) | IrStmt::Skip | IrStmt::Break | IrStmt::Continue => false,
    })
}

fn collect_written(
    stmts: &[IrStmt],
    m: &IrModule,
    all: &[IrModule],
    params: &HashSet<String>,
    out: &mut HashSet<String>,
) {
    for s in stmts {
        match s {
            IrStmt::Assign {
                target,
                value,
                declares,
            } => {
                match target {
                    Place::Var(n) => {
                        if !declares && params.contains(n) {
                            out.insert(n.clone());
                        }
                    }
                    _ => {
                        let r = place_root(target);
                        if params.contains(r) {
                            out.insert(r.to_string());
                        }
                    }
                }
                written_expr(value, m, all, params, out);
            }
            IrStmt::TupleAssign {
                targets,
                declares,
                value,
            } => {
                for (t, d) in targets.iter().zip(declares) {
                    if !d && params.contains(t) {
                        out.insert(t.clone());
                    }
                }
                written_expr(value, m, all, params, out);
            }
            IrStmt::Expr(e) => written_expr(e, m, all, params, out),
            IrStmt::If { arms, else_block } => {
                for (c, b) in arms {
                    written_expr(c, m, all, params, out);
                    collect_written(b, m, all, params, out);
                }
                if let Some(b) = else_block {
                    collect_written(b, m, all, params, out);
                }
            }
            IrStmt::While { cond, body } => {
                written_expr(cond, m, all, params, out);
                collect_written(body, m, all, params, out);
            }
            IrStmt::ForRange {
                from, to, body, ..
            } => {
                written_expr(from, m, all, params, out);
                written_expr(to, m, all, params, out);
                collect_written(body, m, all, params, out);
            }
            IrStmt::ForIn { iter, body, .. } => {
                written_expr(iter, m, all, params, out);
                collect_written(body, m, all, params, out);
            }
            IrStmt::Match { scrutinee, arms } => {
                written_expr(scrutinee, m, all, params, out);
                for a in arms {
                    collect_written(&a.body, m, all, params, out);
                }
            }
            IrStmt::Return(Some(e)) => written_expr(e, m, all, params, out),
            IrStmt::Assert { cond, .. } => written_expr(cond, m, all, params, out),
            IrStmt::ExpectTrap { body, .. } => collect_written(body, m, all, params, out),
            _ => {}
        }
    }
}

fn written_expr(
    e: &IrExpr,
    m: &IrModule,
    all: &[IrModule],
    params: &HashSet<String>,
    out: &mut HashSet<String>,
) {
    match &e.kind {
        IrExprKind::MutBuiltin { recv, args, .. } => {
            let r = place_root(recv);
            if params.contains(r) {
                out.insert(r.to_string());
            }
            for a in args {
                written_expr(a, m, all, params, out);
            }
        }
        IrExprKind::CallFunc { name, args } => {
            if let Some(cf) = resolve_func_in(m, all, name) {
                for (arg, p) in args.iter().zip(&cf.params) {
                    if p.inout {
                        if let Some(r) = expr_root_var(arg) {
                            if params.contains(r) {
                                out.insert(r.to_string());
                            }
                        }
                    }
                }
            }
            for a in args {
                written_expr(a, m, all, params, out);
            }
        }
        IrExprKind::CallValue { callee, args } => {
            written_expr(callee, m, all, params, out);
            for a in args {
                written_expr(a, m, all, params, out);
            }
        }
        IrExprKind::List(xs)
        | IrExprKind::Tuple(xs)
        | IrExprKind::NewRecord { args: xs, .. }
        | IrExprKind::NewVariant { args: xs, .. }
        | IrExprKind::Builtin { args: xs, .. } => {
            for x in xs {
                written_expr(x, m, all, params, out);
            }
        }
        IrExprKind::GetField { recv, .. } => written_expr(recv, m, all, params, out),
        IrExprKind::Index { recv, index } => {
            written_expr(recv, m, all, params, out);
            written_expr(index, m, all, params, out);
        }
        IrExprKind::Unary { operand, .. } => written_expr(operand, m, all, params, out),
        IrExprKind::Binary { lhs, rhs, .. } => {
            written_expr(lhs, m, all, params, out);
            written_expr(rhs, m, all, params, out);
        }
        _ => {}
    }
}

fn expr_as_place(e: &IrExpr) -> Place {
    match &e.kind {
        IrExprKind::Local(n) => Place::Var(n.clone()),
        IrExprKind::GetField { recv, name } => Place::Field {
            base: Box::new(expr_as_place(recv)),
            base_ty: recv.ty.clone(),
            name: name.clone(),
        },
        other => unreachable!("inout arg is not a place: {other:?}"),
    }
}

fn can_trap(e: &IrExpr) -> bool {
    match &e.kind {
        IrExprKind::Int(_)
        | IrExprKind::Float(_)
        | IrExprKind::Bool(_)
        | IrExprKind::Local(_)
        | IrExprKind::Const(_)
        | IrExprKind::FuncRef(_)
        | IrExprKind::Text(_) => false,
        IrExprKind::CallFunc { .. } | IrExprKind::CallValue { .. } => true,
        IrExprKind::Unary { op, operand } => {
            (matches!(op, UnaryOp::Neg) && matches!(operand.ty, Ty::Int)) || can_trap(operand)
        }
        IrExprKind::Binary { op, lhs, rhs } => {
            let arith_trap = matches!(lhs.ty, Ty::Int)
                && matches!(
                    op,
                    BinaryOp::Add
                        | BinaryOp::Sub
                        | BinaryOp::Mul
                        | BinaryOp::Div
                        | BinaryOp::Mod
                );
            arith_trap || can_trap(lhs) || can_trap(rhs)
        }
        IrExprKind::Builtin { builtin, args } => {
            let traps = matches!(
                builtin,
                Builtin::AbsInt
                    | Builtin::IntOfFloat
                    | Builtin::OptUnwrap
                    | Builtin::ResUnwrap
                    | Builtin::ListPop
                    | Builtin::ListRemoveAt
            );
            traps || args.iter().any(can_trap)
        }
        IrExprKind::List(xs) | IrExprKind::Tuple(xs) => xs.iter().any(can_trap),
        IrExprKind::NewRecord { args, .. } | IrExprKind::NewVariant { args, .. } => {
            args.iter().any(can_trap)
        }
        IrExprKind::GetField { recv, .. } => can_trap(recv),
        IrExprKind::Index { .. } | IrExprKind::MutBuiltin { .. } => true,
    }
}

// ── type collection ─────────────────────────────────────────────────────────

#[derive(Default)]
struct TypeSet {
    types: BTreeMap<String, Ty>,
}

impl TypeSet {
    fn add(&mut self, ty: &Ty) {
        match ty {
            Ty::Int | Ty::Float | Ty::Bool | Ty::Infer(_) => {}
            Ty::Func { params, ret } => {
                for p in params {
                    self.add(p);
                }
                if let Some(r) = ret {
                    self.add(r);
                }
            }
            Ty::Map(k, v) => {
                self.add(k);
                self.add(v);
                self.types.insert(mangle(ty), ty.clone());
            }
            Ty::Set(e) => {
                self.add(e);
                self.types.insert(mangle(ty), ty.clone());
            }
            Ty::List(e) => {
                self.add(e);
                self.types.insert(mangle(ty), ty.clone());
            }
            Ty::Tuple(ts) => {
                for t in ts {
                    self.add(t);
                }
                if !ts.is_empty() {
                    self.types.insert(mangle(ty), ty.clone());
                }
            }
            Ty::Option_(e) => {
                self.add(e);
                self.types.insert(mangle(ty), ty.clone());
            }
            Ty::Result_(a, b) => {
                self.add(a);
                self.add(b);
                self.types.insert(mangle(ty), ty.clone());
            }
            Ty::Record(n) | Ty::Enum(n) => {
                self.types.insert(n.clone(), ty.clone());
            }
        }
    }
}

fn collect_types(m: &IrModule) -> TypeSet {
    let mut set = TypeSet::default();
    for r in &m.records {
        set.add(&Ty::Record(r.name.clone()));
        for (_, t) in &r.fields {
            set.add(t);
        }
    }
    for e in &m.enums {
        set.add(&Ty::Enum(e.name.clone()));
        for v in &e.variants {
            for (_, t) in &v.fields {
                set.add(t);
            }
        }
    }
    for c in &m.consts {
        set.add(&c.ty);
    }
    for f in &m.funcs {
        for p in &f.params {
            set.add(&p.ty);
        }
        if let Some(r) = &f.ret {
            set.add(r);
        }
        walk_stmts(&f.body, &mut set);
    }
    for t in &m.tests {
        walk_stmts(&t.body, &mut set);
    }
    set
}

fn collect_types_all(modules: &[IrModule]) -> TypeSet {
    let mut set = TypeSet::default();
    for m in modules {
        set.types.extend(collect_types(m).types);
    }
    set
}

/// Emit the shared `sudo_types.zig` file: full declarations for every portable
/// monomorphized type (and its copy/eq/canon/keyapp helpers).
fn emit_shared_types(shared: &TypeSet) -> String {
    let dummy = IrModule {
        name: "sudo_types".into(),
        imports: Vec::new(),
        records: Vec::new(),
        enums: Vec::new(),
        consts: Vec::new(),
        funcs: Vec::new(),
        tests: Vec::new(),
    };
    let empty_hoist = BTreeSet::new();
    let mut emitter = Emitter {
        m: &dummy,
        all: &[],
        out: String::new(),
        indent: 0,
        tmp: 0,
        inouts: HashSet::new(),
        is_entry: false,
        trap_label: None,
        hoisted: &empty_hoist,
    };
    emitter.line(
        "// Generated by sudoc — canonical identity for cross-module monomorphized",
    );
    emitter.line(
        "// generic instantiations; every {module}.zig imports this file rather",
    );
    emitter.line(
        "// than re-declaring these types locally. Do not edit; edit the sudo",
    );
    emitter.line("// source.");
    emitter.line("const rt = @import(\"sudo_rt.zig\");");
    emitter.blank();
    emitter.emit_type_section(shared);
    emitter.out
}

fn walk_stmts(stmts: &[IrStmt], set: &mut TypeSet) {
    for s in stmts {
        match s {
            IrStmt::Assign { target, value, .. } => {
                walk_place(target, set);
                walk_expr(value, set);
            }
            IrStmt::TupleAssign { value, .. } => walk_expr(value, set),
            IrStmt::Expr(e) => walk_expr(e, set),
            IrStmt::If { arms, else_block } => {
                for (c, b) in arms {
                    walk_expr(c, set);
                    walk_stmts(b, set);
                }
                if let Some(b) = else_block {
                    walk_stmts(b, set);
                }
            }
            IrStmt::While { cond, body } => {
                walk_expr(cond, set);
                walk_stmts(body, set);
            }
            IrStmt::ForRange { from, to, body, .. } => {
                walk_expr(from, set);
                walk_expr(to, set);
                walk_stmts(body, set);
            }
            IrStmt::ForIn { iter, body, .. } => {
                walk_expr(iter, set);
                walk_stmts(body, set);
            }
            IrStmt::Match { scrutinee, arms } => {
                walk_expr(scrutinee, set);
                for a in arms {
                    walk_stmts(&a.body, set);
                }
            }
            IrStmt::Return(Some(e)) => walk_expr(e, set),
            IrStmt::Assert { cond, .. } => walk_expr(cond, set),
            IrStmt::ExpectTrap { body, .. } => walk_stmts(body, set),
            IrStmt::Return(None) | IrStmt::Skip | IrStmt::Break | IrStmt::Continue => {}
        }
    }
}

fn walk_expr(e: &IrExpr, set: &mut TypeSet) {
    set.add(&e.ty);
    match &e.kind {
        IrExprKind::List(xs)
        | IrExprKind::Tuple(xs)
        | IrExprKind::CallFunc { args: xs, .. }
        | IrExprKind::NewRecord { args: xs, .. }
        | IrExprKind::NewVariant { args: xs, .. }
        | IrExprKind::Builtin { args: xs, .. } => xs.iter().for_each(|x| walk_expr(x, set)),
        IrExprKind::CallValue { callee, args } => {
            walk_expr(callee, set);
            args.iter().for_each(|x| walk_expr(x, set));
        }
        IrExprKind::MutBuiltin {
            recv,
            recv_ty,
            args,
            ..
        } => {
            set.add(recv_ty);
            walk_place(recv, set);
            args.iter().for_each(|x| walk_expr(x, set));
        }
        IrExprKind::GetField { recv, .. } => walk_expr(recv, set),
        IrExprKind::Index { recv, index } => {
            walk_expr(recv, set);
            walk_expr(index, set);
        }
        IrExprKind::Unary { operand, .. } => walk_expr(operand, set),
        IrExprKind::Binary { lhs, rhs, .. } => {
            walk_expr(lhs, set);
            walk_expr(rhs, set);
        }
        _ => {}
    }
}

fn walk_place(p: &Place, set: &mut TypeSet) {
    match p {
        Place::Var(_) => {}
        Place::Index {
            base,
            base_ty,
            index,
        } => {
            set.add(base_ty);
            walk_place(base, set);
            walk_expr(index, set);
        }
        Place::Field { base, base_ty, .. } => {
            set.add(base_ty);
            walk_place(base, set);
        }
    }
}

fn topo_order(set: &TypeSet, m: &IrModule) -> Vec<String> {
    fn complete_deps(ty: &Ty, m: &IrModule, out: &mut Vec<Ty>) {
        match ty {
            Ty::List(e) => {
                if !is_scalar(e) {
                    out.push((**e).clone());
                }
            }
            Ty::Option_(e) => {
                if !is_scalar(e) {
                    out.push((**e).clone());
                }
            }
            Ty::Result_(t, e) => {
                if !is_scalar(t) {
                    out.push((**t).clone());
                }
                if !is_scalar(e) {
                    out.push((**e).clone());
                }
            }
            Ty::Tuple(ts) => out.extend(ts.iter().cloned()),
            Ty::Record(name) => {
                if let Some(r) = m.records.iter().find(|r| r.name == *name) {
                    out.extend(r.fields.iter().map(|(_, t)| t.clone()));
                }
            }
            Ty::Enum(name) => {
                if let Some(e) = m.enums.iter().find(|e| e.name == *name) {
                    for v in &e.variants {
                        for (_, t) in &v.fields {
                            out.push(t.clone());
                        }
                    }
                }
            }
            _ => {}
        }
        out.retain(|t| !is_scalar(t));
    }

    let mut order = Vec::new();
    let mut done: BTreeSet<String> = BTreeSet::new();
    fn visit(
        name: &str,
        set: &TypeSet,
        m: &IrModule,
        done: &mut BTreeSet<String>,
        order: &mut Vec<String>,
    ) {
        if done.contains(name) {
            return;
        }
        done.insert(name.to_string());
        let ty = &set.types[name];
        let mut deps = Vec::new();
        complete_deps(ty, m, &mut deps);
        for d in deps {
            let dn = mangle(&d);
            if set.types.contains_key(&dn) {
                visit(&dn, set, m, done, order);
            }
        }
        order.push(name.to_string());
    }
    for name in set.types.keys() {
        visit(name, set, m, &mut done, &mut order);
    }
    order
}

/// Fold a const initializer to a Zig comptime literal (wrapping arith; the
/// checker already rejected overflowing const expressions).
fn fold_const(e: &IrExpr, m: &IrModule) -> String {
    #[derive(Clone, Copy)]
    enum V {
        I(i64),
        F(f64),
        B(bool),
    }
    fn go(e: &IrExpr, m: &IrModule) -> V {
        match &e.kind {
            IrExprKind::Int(v) => V::I(*v),
            IrExprKind::Float(v) => V::F(*v),
            IrExprKind::Bool(v) => V::B(*v),
            IrExprKind::Const(n) => {
                let c = m
                    .consts
                    .iter()
                    .find(|c| c.name == *n)
                    .expect("const exists");
                go(&c.value, m)
            }
            IrExprKind::Unary {
                op: UnaryOp::Neg,
                operand,
            } => match go(operand, m) {
                V::I(v) => V::I(v.wrapping_neg()),
                V::F(v) => V::F(-v),
                V::B(_) => unreachable!(),
            },
            IrExprKind::Binary { op, lhs, rhs } => {
                let (l, r) = (go(lhs, m), go(rhs, m));
                match (l, r) {
                    (V::I(a), V::I(b)) => V::I(match op {
                        BinaryOp::Add => a.wrapping_add(b),
                        BinaryOp::Sub => a.wrapping_sub(b),
                        BinaryOp::Mul => a.wrapping_mul(b),
                        BinaryOp::Div => {
                            let q = a.wrapping_div(b);
                            if a % b != 0 && (a < 0) != (b < 0) {
                                q.wrapping_sub(1)
                            } else {
                                q
                            }
                        }
                        BinaryOp::Mod => {
                            let r = a % b;
                            if r != 0 && (r < 0) != (b < 0) {
                                r.wrapping_add(b)
                            } else {
                                r
                            }
                        }
                        _ => unreachable!(),
                    }),
                    (V::F(a), V::F(b)) => V::F(match op {
                        BinaryOp::Add => a + b,
                        BinaryOp::Sub => a - b,
                        BinaryOp::Mul => a * b,
                        BinaryOp::Div => a / b,
                        _ => unreachable!(),
                    }),
                    _ => unreachable!(),
                }
            }
            _ => unreachable!("checker restricts const exprs"),
        }
    }
    match go(e, m) {
        V::I(v) => format!("{v}"),
        V::F(v) => format_float(v),
        V::B(v) => {
            if v {
                "true".into()
            } else {
                "false".into()
            }
        }
    }
}

fn format_float(v: f64) -> String {
    if v.is_nan() {
        return "rt.nan()".into();
    }
    if v.is_infinite() {
        return if v.is_sign_positive() {
            "rt.inf()".into()
        } else {
            "(-rt.inf())".into()
        };
    }
    if v == 0.0 && v.is_sign_negative() {
        return "-0.0".into();
    }
    let s = format!("{v:?}");
    if s.contains('.') || s.contains('e') || s.contains('E') {
        s
    } else {
        format!("{s}.0")
    }
}

/// The Zig backend, via the SDK contract.
pub struct ZigBackend;

impl Backend for ZigBackend {
    fn name(&self) -> &'static str {
        "zig"
    }

    fn emit_program(
        &self,
        modules: &[IrModule],
        with_tests: bool,
    ) -> Result<Vec<GeneratedFile>, String> {
        // Escape reserved identifiers first so type-hoisting keys (which
        // embed record/enum names) match what emit_with later produces.
        let modules: Vec<IrModule> = modules.iter().map(reserved::rename_reserved).collect();
        let (entry, deps) = modules.split_last().expect("entry module");
        // Single-module programs keep fully local monomorphs (byte-identical
        // to pre-hoist output). Multi-module: hoist every portable shape so
        // cross-module calls share one nominal Zig type identity.
        let all_types = if deps.is_empty() {
            TypeSet::default()
        } else {
            collect_types_all(&modules)
        };
        let hoisted: BTreeSet<String> = if deps.is_empty() {
            BTreeSet::new()
        } else {
            all_types
                .types
                .iter()
                .filter(|(_, ty)| is_portable(ty))
                .map(|(k, _)| k.clone())
                .collect()
        };
        let mut out = Vec::new();
        if !hoisted.is_empty() {
            let shared = TypeSet {
                types: all_types
                    .types
                    .iter()
                    .filter(|(k, _)| hoisted.contains(*k))
                    .map(|(k, v)| (k.clone(), v.clone()))
                    .collect(),
            };
            out.push(GeneratedFile {
                path: "sudo_types.zig".into(),
                contents: emit_shared_types(&shared),
            });
        }
        for m in deps {
            out.push(GeneratedFile {
                path: format!("{}.zig", m.name),
                contents: emit_with(m, &modules, false, false, &hoisted),
            });
        }
        out.push(GeneratedFile {
            path: format!("{}.zig", entry.name),
            contents: emit_with(entry, &modules, with_tests, true, &hoisted),
        });
        Ok(out)
    }

    fn runtime_files(&self) -> Vec<GeneratedFile> {
        vec![GeneratedFile {
            path: RUNTIME_FILE.into(),
            contents: RUNTIME.into(),
        }]
    }

    fn test_recipe(&self, entry: &str) -> TestRecipe {
        TestRecipe {
            build: vec![vec![
                "zig".into(),
                "build-exe".into(),
                format!("{entry}.zig"),
                "-femit-bin=sudo_tests".into(),
                "-lc".into(),
                "-O".into(),
                "ReleaseSafe".into(),
            ]],
            run: vec!["./sudo_tests".into()],
        }
    }
}
