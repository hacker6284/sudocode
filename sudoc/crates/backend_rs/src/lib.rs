impl Emitter<'_> {
    fn mut_builtin_ty(
        &mut self,
        b: Builtin,
        recv: &Place,
        recv_ty: &Ty,
        args: &[IrExpr],
    ) -> String {
        if b == Builtin::ListSort {
            let br = self.as_mut_ref(recv);
            match strip(recv_ty) {
                Ty::List(elem) if matches!(strip(elem), Ty::Float) => {
                    return format!("crate::sudo_rt::sort_floats({br})");
                }
                _ => {
                    // Ord types (i64): stable sort via slice.
                    return format!("({br}).sort()");
                }
            }
        }
        self.mut_builtin(b, recv, args)
    }
}

