impl sudoc_sdk::Backend for RsBackend {
    fn name(&self) -> &'static str {
        "rs"
    }

    fn emit_program(
        &self,
        modules: &[IrModule],
        with_tests: bool,
    ) -> Result<Vec<sudoc_sdk::GeneratedFile>, String> {
        let (entry, deps) = modules.split_last().expect("entry module");
        let mut out = Vec::new();
        for m in deps {
            out.push(sudoc_sdk::GeneratedFile {
                path: module_file(&m.name),
                contents: emit(m, false, false),
            });
        }
        out.push(sudoc_sdk::GeneratedFile {
            path: module_file(&entry.name),
            contents: emit(entry, with_tests, true),
        });
        Ok(out)
    }

    fn runtime_files(&self) -> Vec<sudoc_sdk::GeneratedFile> {
        vec![sudoc_sdk::GeneratedFile {
            path: RUNTIME_FILE.into(),
            contents: RUNTIME.into(),
        }]
    }

    fn test_recipe(&self, entry: &str) -> sudoc_sdk::TestRecipe {
        sudoc_sdk::TestRecipe {
            build: vec![vec![
                "rustc".into(),
                "--edition".into(),
                "2021".into(),
                "-A".into(),
                "warnings".into(),
                "-o".into(),
                "sudo_tests".into(),
                format!("{entry}.rs"),
            ]],
            run: vec!["./sudo_tests".into()],
        }
    }
}
