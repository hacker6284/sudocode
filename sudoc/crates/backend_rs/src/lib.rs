fn aliasing(kind: &IrExprKind) -> bool {
    match kind {
        IrExprKind::Local(_)
        | IrExprKind::Const(_)
        | IrExprKind::GetField { .. }
        | IrExprKind::Index { .. } => true,
        IrExprKind::Builtin { builtin, .. } => matches!(
            builtin,
            Builtin::OptUnwrap
                | Builtin::OptGetOr
                | Builtin::ResUnwrap
                | Builtin::ResGetOr
        ),
        _ => false,
    }
}

/// Types that need `.clone()` at owning store points.
/// Enum is included (Rust move semantics) — deliberate departure from JS.
fn needs_dup(ty: &Ty) -> bool {
    match ty {
        Ty::List(_) | Ty::Map(..) | Ty::Set(_) | Ty::Record(_) | Ty::Enum(_) => true,
        Ty::Option_(t) => needs_dup(t),
        Ty::Result_(t, e) => needs_dup(t) || needs_dup(e),
        Ty::Tuple(ts) => ts.iter().any(needs_dup),
        _ => false,
    }
}

fn strip(ty: &Ty) -> &Ty {
    ty
}

/// Cross-module `dep.fn` → `dep::fn`; bare names unchanged.
fn qual_name(name: &str) -> String {
    if name.contains('.') {
        name.replace('.', "::")
    } else {
        name.to_string()
    }
}

/// Box enum-payload fields of these kinds (same rule as backend_c::boxed_in_payload).
fn boxed_in_payload(ty: &Ty) -> bool {
    matches!(
        ty,
        Ty::Record(_) | Ty::Enum(_) | Ty::Option_(_) | Ty::Result_(..) | Ty::Tuple(_)
    )
}

fn ty_str(ty: &Ty) -> String {
    match ty {
        Ty::Int => "i64".into(),
        Ty::Float => "f64".into(),
        Ty::Bool => "bool".into(),
        Ty::List(t) => format!("Vec<{}>", ty_str(t)),
        Ty::Set(t) => format!("std::collections::HashSet<{}>", ty_str(t)),
        Ty::Map(k, v) => format!(
            "std::collections::HashMap<{}, {}>",
            ty_str(k),
            ty_str(v)
        ),
        Ty::Option_(t) => format!("Option<{}>", ty_str(t)),
        Ty::Result_(t, e) => format!("Result<{}, {}>", ty_str(t), ty_str(e)),
        Ty::Tuple(ts) => {
            let parts: Vec<String> = ts.iter().map(ty_str).collect();
            if parts.len() == 1 {
                format!("({},)", parts[0])
            } else {
                format!("({})", parts.join(", "))
            }
        }
        Ty::Func { params, ret } => {
            let ps: Vec<String> = params.iter().map(ty_str).collect();
            match ret {
                Some(r) => format!("fn({}) -> {}", ps.join(", "), ty_str(r)),
                None => format!("fn({})", ps.join(", ")),
            }
        }
        Ty::Record(n) | Ty::Enum(n) => n.clone(),
        Ty::Infer(_) => "()".into(),
    }
}

fn type_is_hashable(ty: &Ty, m: &IrModule, seen: &mut Vec<String>) -> bool {
    match ty {
        Ty::Int | Ty::Bool => true,
        Ty::Float | Ty::Map(..) | Ty::Set(_) | Ty::Func { .. } | Ty::Infer(_) => false,
        Ty::List(t) | Ty::Option_(t) => type_is_hashable(t, m, seen),
        Ty::Result_(t, e) => type_is_hashable(t, m, seen) && type_is_hashable(e, m, seen),
        Ty::Tuple(ts) => ts.iter().all(|t| type_is_hashable(t, m, seen)),
        Ty::Record(name) => {
            if seen.contains(name) {
                return true;
            }
            seen.push(name.clone());
            m.record(name)
                .is_some_and(|r| r.fields.iter().all(|(_, t)| type_is_hashable(t, m, seen)))
        }
        Ty::Enum(name) => {
            if seen.contains(name) {
                return true;
            }
            seen.push(name.clone());
            m.enum_(name).is_some_and(|e| {
                e.variants
                    .iter()
                    .all(|v| v.fields.iter().all(|(_, t)| type_is_hashable(t, m, seen)))
            })
        }
    }
}

/// Escape a string for embedding as a Rust `"..."` literal (no surrounding quotes).
fn escape_rust_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            c => out.push(c),
        }
    }
    out
}

fn rust_float_lit(v: f64) -> String {
    if v.is_nan() {
        return "f64::NAN".into();
    }
    if v.is_infinite() {
        return if v.is_sign_positive() {
            "f64::INFINITY".into()
        } else {
            "f64::NEG_INFINITY".into()
        };
    }
    if v == 0.0 && v.is_sign_negative() {
        return "-0.0f64".into();
    }
    let s = format!("{v:?}");
    if !s.contains('.') && !s.contains('e') && !s.contains('E') {
        format!("{s}.0f64")
    } else if s.ends_with("f64") {
        s
    } else {
        format!("{s}f64")
    }
}

